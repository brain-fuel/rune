package codegen

import (
	"fmt"
	"strings"

	"goforge.dev/rune/v3/core"
)

// JS is the v1 backend: boring JavaScript. Everything is curried one-argument
// arrow functions; constructors build {tag, args} records; eliminators are
// switch-dispatch with recursive calls for the inductive hypotheses; the
// erased token is null. The output runs under node with no dependencies.
type JS struct{}

func (JS) Target() string { return "js" }

// Emit lowers a program to a self-contained JavaScript source file.
func (JS) Emit(p Program) (TargetSource, error) {
	em := &jsEmitter{}
	if p.Nat != nil {
		em.natElim = p.Nat.ElimName
		em.accel = p.Nat.Ops
	}
	var b strings.Builder
	b.WriteString("// Generated by rune emit — the erased shadow of checked core. Do not edit.\n")
	b.WriteString("'use strict';\n")
	b.WriteString("const $unit = null;\n")

	if usesQuot(p) {
		b.WriteString(quotRuntime)
	}
	if usesOTP(p) || usesLiveKV(p) || usesNet(p) || usesTLS(p) {
		// An OTP program (primReceive blocks), a live-broker program, a Phase-1
		// socket program, or a TLS program (Node TCP/TLS is async) needs the ASYNC IO
		// monad (bindIO awaits).
		// Contained to these programs: ordinary IO keeps the synchronous runtime, so
		// all other js conformance is untouched.
		b.WriteString(ioRuntimeAsync)
		if usesOTP(p) {
			b.WriteString(jsOTPRuntime)
		}
		if usesLiveKV(p) {
			b.WriteString(jsLiveKVRuntime)
		}
		if usesNet(p) {
			b.WriteString(jsNetRuntime)
		}
	} else if usesIO(p) {
		b.WriteString(ioRuntime)
	}
	// R-EFFECT (Task 6): the seeded cooperative frontier scheduler for `par`.
	// Same 64-bit LCG as the Go backend (via BigInt) so a fixed WAVELET_SEED
	// yields a byte-identical interleaving on both backends.
	if usesPar(p) {
		b.WriteString(jsFrontierRuntime)
	}
	// D6 / R-EFFECT: bake the standard OS/IO host bodies when referenced.
	if usesForeign(p, "printNat") {
		b.WriteString("const printNat = () => n => () => { console.log(n.toString()); return n; };\n")
	}
	// Phase 0 — real byte strings (Bin). A Bin is a JS Array of byte Numbers (NOT a
	// JS string, whose UTF-16 reindexes astral codepoints); nats cross as BigInt.
	if usesBin(p) {
		b.WriteString("const binEmpty = () => [];\n")
		b.WriteString("const binCons = () => c => b => [Number(c) & 255].concat(b);\n")
		b.WriteString("const binLen = () => b => BigInt(b.length);\n")
		b.WriteString("const binAt = () => b => i => { const k = Number(i); return (k >= 0 && k < b.length) ? BigInt(b[k]) : 0n; };\n")
		b.WriteString("const __binShow = b => { let s = '\"'; for (const x of b) { if (x >= 0x20 && x < 0x7f && x !== 0x22 && x !== 0x5c) { s += globalThis.String.fromCharCode(x); } else { s += '\\\\x' + x.toString(16).padStart(2, '0'); } } return s + '\"'; };\n")
		b.WriteString("const printBin = () => b => () => { console.log(__binShow(b)); return $unit; };\n")
	}
	// Phase 1 — expanded OS/filesystem layer (Bin-native). Like getNat, fs access uses
	// dynamic import when the file is an ES module (an `import` present => async IO).
	if usesFS(p) {
		if usesOTP(p) || usesLiveKV(p) || usesNet(p) || usesTLS(p) {
			b.WriteString("const fsWrite = () => path => content => async () => { const fs = await import('node:fs'); try { fs.writeFileSync(Buffer.from(path).toString('latin1'), Buffer.from(content)); return BigInt(content.length); } catch (e) { return 0n; } };\n")
			b.WriteString("const fsRead = () => path => async () => { const fs = await import('node:fs'); try { return Array.from(fs.readFileSync(Buffer.from(path).toString('latin1'))); } catch (e) { return []; } };\n")
			b.WriteString("const fsExists = () => path => async () => { const fs = await import('node:fs'); return fs.existsSync(Buffer.from(path).toString('latin1')) ? 1n : 0n; };\n")
			b.WriteString("const fsRemove = () => path => async () => { const fs = await import('node:fs'); try { fs.unlinkSync(Buffer.from(path).toString('latin1')); return 1n; } catch (e) { return 0n; } };\n")
			b.WriteString("const fsMkdir = () => path => async () => { const fs = await import('node:fs'); try { fs.mkdirSync(Buffer.from(path).toString('latin1'), { recursive: true }); return 1n; } catch (e) { return 0n; } };\n")
		} else {
			b.WriteString("const fsWrite = () => path => content => () => { const fs = require('fs'); try { fs.writeFileSync(Buffer.from(path).toString('latin1'), Buffer.from(content)); return BigInt(content.length); } catch (e) { return 0n; } };\n")
			b.WriteString("const fsRead = () => path => () => { const fs = require('fs'); try { return Array.from(fs.readFileSync(Buffer.from(path).toString('latin1'))); } catch (e) { return []; } };\n")
			b.WriteString("const fsExists = () => path => () => { const fs = require('fs'); return fs.existsSync(Buffer.from(path).toString('latin1')) ? 1n : 0n; };\n")
			b.WriteString("const fsRemove = () => path => () => { const fs = require('fs'); try { fs.unlinkSync(Buffer.from(path).toString('latin1')); return 1n; } catch (e) { return 0n; } };\n")
			b.WriteString("const fsMkdir = () => path => () => { const fs = require('fs'); try { fs.mkdirSync(Buffer.from(path).toString('latin1'), { recursive: true }); return 1n; } catch (e) { return 0n; } };\n")
		}
	}
	// Phase 1 — os/exec: run program with one arg, capture stdout (ESM-gated like fs).
	if usesProc(p) {
		if usesOTP(p) || usesLiveKV(p) || usesNet(p) || usesTLS(p) {
			b.WriteString("const procRun = () => prog => arg => async () => { const cp = await import('node:child_process'); try { return Array.from(cp.execFileSync(Buffer.from(prog).toString('latin1'), [Buffer.from(arg).toString('latin1')])); } catch (e) { return []; } };\n")
		} else {
			b.WriteString("const procRun = () => prog => arg => () => { const cp = require('child_process'); try { return Array.from(cp.execFileSync(Buffer.from(prog).toString('latin1'), [Buffer.from(arg).toString('latin1')])); } catch (e) { return []; } };\n")
		}
	}
	// Phase 3 — crypto: host sha256 (pure; createHash is synchronous, so ESM uses a
	// top-level import rather than the async pattern the IO prims use).
	if usesCrypto(p) {
		if usesOTP(p) || usesLiveKV(p) || usesNet(p) || usesTLS(p) {
			b.WriteString("import * as __cryptoM from 'node:crypto';\n")
			b.WriteString("const sha256 = () => data => Array.from(__cryptoM.createHash('sha256').update(Buffer.from(data)).digest());\n")
		} else {
			b.WriteString("const sha256 = () => data => Array.from(require('crypto').createHash('sha256').update(Buffer.from(data)).digest());\n")
		}
	}
	// Phase 3 — TLS: HTTPS GET (async), return the response body (cert verify off).
	if usesTLS(p) {
		b.WriteString("import * as __httpsM from 'node:https';\n")
		b.WriteString("const tlsGet = () => host => port => path => () => new Promise(res => { const req = __httpsM.request({ host: Buffer.from(host).toString('latin1'), port: Number(port), path: Buffer.from(path).toString('latin1'), method: 'GET', rejectUnauthorized: false }, r => { const chunks = []; r.on('data', d => chunks.push(d)); r.on('end', () => res(Array.from(Buffer.concat(chunks)))); }); req.on('error', () => res([])); req.end(); });\n")
	}
	if usesForeign(p, "getNat") {
		if usesOTP(p) || usesLiveKV(p) || usesNet(p) || usesTLS(p) {
			// An `import` makes the file an ES module, where `require` is undefined; but
			// these programs run under the async IO monad, so getNat can be async and
			// pull fs via dynamic import (awaited by bindIO).
			b.WriteString("const getNat = () => async () => { const fs = await import('node:fs'); return BigInt(fs.readFileSync(0, 'utf8').trim().split(/\\s+/)[0]); };\n")
		} else {
			b.WriteString("const getNat = () => () => BigInt(require('fs').readFileSync(0, 'utf8').trim().split(/\\s+/)[0]);\n")
		}
	}
	if usesForeign(p, "timeNanos") {
		b.WriteString("const timeNanos = () => () => BigInt(Date.now()) * 1000000n;\n")
	}
	if usesForeign(p, "readLineCode") {
		b.WriteString("const readLineCode = () => () => { const s = require('fs').readFileSync(0, 'utf8').split('\\n')[0]; let n = 1n; for (let i = s.length - 1; i >= 0; i--) n = n * 256n + BigInt(s.charCodeAt(i)); return n; };\n")
	}
	// D6 net/fs: the packed-String codec (decode code->string, encode string->code)
	// + env/file host bodies, over bare Nat codes (the Rune side wraps `bytes`).
	if usesFileEnv(p) || usesLiveKV(p) || usesStream(p) || usesForeign(p, "parseFloat") {
		// globalThis.String (NOT bare `String`): a Rune `String : U` def emits a
		// top-level `const String` that would shadow the JS builtin.
		b.WriteString("const __s2h = n => { let s = ''; while (n > 1n) { s += globalThis.String.fromCharCode(Number(n % 256n)); n = n / 256n; } return s; };\n")
		b.WriteString("const __h2s = v => { let n = 1n; for (let i = v.length - 1; i >= 0; i--) n = n * 256n + BigInt(v.charCodeAt(i)); return n; };\n")
	}
	if usesForeign(p, "byteLen") {
		b.WriteString("const byteLen = () => c => BigInt(__s2h(c).length);\n")
	}
	if usesForeign(p, "splitOn") {
		b.WriteString("const splitOn = () => sep => c => { const parts = __s2h(c).split(globalThis.String.fromCharCode(Number(sep))); let lst = {tag:0,name:\"nil\",args:[null]}; for (let i = parts.length-1; i>=0; i--) lst = {tag:1,name:\"cons\",args:[null,__h2s(parts[i]),lst]}; return lst; };\n")
	}
	if usesForeign(p, "jsonStrField") {
		b.WriteString("const jsonStrField = () => field => doc => { const fn = __s2h(field), ds = __s2h(doc); const needle = '\"'+fn+'\"'; const none = {tag:0,name:\"none\",args:[null]}; let i = ds.indexOf(needle); if (i<0) return none; let j = i+needle.length; while (j<ds.length && (ds[j]===' '||ds[j]==='\\t'||ds[j]===':')) j++; if (ds[j]==='\"') { j++; let k=j; while(k<ds.length && ds[k]!=='\"') k++; return {tag:1,name:\"some\",args:[null,__h2s(ds.slice(j,k))]}; } return none; };\n")
	}
	if usesForeign(p, "sqlQuote") {
		b.WriteString("const sqlQuote = () => s => { const str = __s2h(s); let out = \"'\"; for (let i = 0; i < str.length; i++) { if (str[i] === \"'\") out += \"'\"; out += str[i]; } out += \"'\"; return __h2s(out); };\n")
	}
	if usesForeign(p, "dbApply") {
		b.WriteString("const dbApply = () => db => sql => () => { try { require('child_process').execFileSync('sqlite3', [__s2h(db), '.read ' + __s2h(sql)]); } catch (e) {} return null; };\n")
	}
	if usesForeign(p, "Handle") {
		b.WriteString("const Handle = () => null;\n")
	}
	if usesForeign(p, "openWrite") {
		b.WriteString("const openWrite = () => path => () => { try { return require('fs').openSync(__s2h(path), 'w'); } catch (e) { return null; } };\n")
	}
	if usesForeign(p, "writeChunk") {
		b.WriteString("const writeChunk = () => h => c => () => { if (h !== null) require('fs').writeSync(h, Buffer.from(__s2h(c) + '\\n', 'latin1')); return h; };\n")
	}
	if usesForeign(p, "closeWrite") {
		b.WriteString("const closeWrite = () => h => () => { if (h !== null) require('fs').closeSync(h); return null; };\n")
	}
	if usesForeign(p, "sortFile") {
		b.WriteString("const sortFile = () => inp => outp => () => { const fs = require('fs'); let data; try { data = fs.readFileSync(__s2h(inp), 'latin1'); } catch (e) { fs.writeFileSync(__s2h(outp), ''); return null; } let lines = data.split('\\n'); if (lines.length && lines[lines.length-1] === '') lines.pop(); lines.sort((a,b) => a < b ? -1 : a > b ? 1 : 0); fs.writeFileSync(__s2h(outp), Buffer.from(lines.map(l => l + '\\n').join(''), 'latin1')); return null; };\n")
	}
	if usesForeign(p, "foldLines") {
		b.WriteString("const foldLines = () => _S => path => step => s0 => () => { let data; try { data = require('fs').readFileSync(__s2h(path), 'latin1'); } catch (e) { return s0; } const lines = data.split('\\n'); if (lines.length && lines[lines.length-1] === '') lines.pop(); let s = s0; for (const ln of lines) s = step(s)(__h2s(ln))(); return s; };\n")
	}
	if usesForeign(p, "foldDir") {
		b.WriteString("const foldDir = () => _S => dir => suf => step => s0 => () => { const fs = require('fs'); const path = require('path'); const sfx = __s2h(suf); let s = s0; const walk = (dd) => { let ents; try { ents = fs.readdirSync(dd, {withFileTypes:true}); } catch (e) { return; } ents.sort((a,b) => a.name < b.name ? -1 : a.name > b.name ? 1 : 0); for (const en of ents) { const full = path.join(dd, en.name); if (en.isDirectory()) walk(full); else if (full.endsWith(sfx)) { let data; try { data = fs.readFileSync(full, 'latin1'); } catch (_) { continue; } s = step(s)(__h2s(data))(); } } }; walk(__s2h(dir)); return s; };\n")
	}
	if usesForeign(p, "getEnvCode") {
		b.WriteString("const getEnvCode = () => c => () => __h2s(process.env[__s2h(c)] || '');\n")
	}
	if usesForeign(p, "readFileCode") {
		b.WriteString("const readFileCode = () => c => () => { try { return __h2s(require('fs').readFileSync(__s2h(c), 'latin1')); } catch (e) { return 1n; } };\n")
	}
	if usesForeign(p, "writeFileCode") {
		b.WriteString("const writeFileCode = () => p => c => () => { require('fs').writeFileSync(__s2h(p), Buffer.from(__s2h(c), 'latin1')); return c; };\n")
	}
	if usesForeign(p, "printStrCode") {
		b.WriteString("const printStrCode = () => c => () => { console.log(__s2h(c)); return c; };\n")
	}
	// D6 argv + process.
	if usesForeign(p, "argCountCode") {
		b.WriteString("const argCountCode = () => () => BigInt(process.argv.length - 2);\n")
	}
	if usesForeign(p, "argAtCode") {
		b.WriteString("const argAtCode = () => i => () => { const a = process.argv[Number(i) + 2]; return a === undefined ? 1n : __h2s(a); };\n")
	}
	if usesForeign(p, "exitWith") {
		b.WriteString("const exitWith = () => n => () => { process.exit(Number(n)); };\n")
	}
	// E4 / wavelet — the LOCAL in-process KV backend (the lib/infra/kv data plane).
	// kvPutCode/kvGetCode/kvDelCode store the packed-String CODE (a BigInt) in a
	// module-global Map, so a program using the kv abstraction RUNS unaided via
	// `rune run --target js` (no broker). The managed-Redis / Valkey bindings are
	// ports of this shape over a real client. An absent key reads back as code 0,
	// which the Rune side's `leW code 1` treats as empty.
	if usesForeign(p, "kvPutCode") || usesForeign(p, "kvGetCode") || usesForeign(p, "kvDelCode") {
		b.WriteString("const __kv = new Map();\n")
	}
	if usesForeign(p, "kvPutCode") {
		b.WriteString("const kvPutCode = () => k => v => () => { __kv.set(k.toString(), v); return v; };\n")
	}
	if usesForeign(p, "kvGetCode") {
		b.WriteString("const kvGetCode = () => k => () => { const x = __kv.get(k.toString()); return x === undefined ? 0n : x; };\n")
	}
	if usesForeign(p, "kvDelCode") {
		b.WriteString("const kvDelCode = () => k => () => { __kv.delete(k.toString()); return 0n; };\n")
	}
	// E4 / wavelet — the local OBJECT store (same map shape as kv, distinct global).
	if usesForeign(p, "objPutCode") || usesForeign(p, "objGetCode") || usesForeign(p, "objDelCode") {
		b.WriteString("const __obj = new Map();\n")
	}
	if usesForeign(p, "objPutCode") {
		b.WriteString("const objPutCode = () => k => v => () => { __obj.set(k.toString(), v); return v; };\n")
	}
	if usesForeign(p, "objGetCode") {
		b.WriteString("const objGetCode = () => k => () => { const x = __obj.get(k.toString()); return x === undefined ? 0n : x; };\n")
	}
	if usesForeign(p, "objDelCode") {
		b.WriteString("const objDelCode = () => k => () => { __obj.delete(k.toString()); return 0n; };\n")
	}
	// E4 / wavelet — the local QUEUE (a FIFO list per queue code). dequeue of an empty
	// queue reads back code 0 (which `leW code 1` treats as empty).
	if usesForeign(p, "enqueueCode") || usesForeign(p, "dequeueCode") {
		b.WriteString("const __q = new Map();\n")
	}
	if usesForeign(p, "enqueueCode") {
		b.WriteString("const enqueueCode = () => q => m => () => { const kk = q.toString(); if (!__q.has(kk)) __q.set(kk, []); __q.get(kk).push(m); return m; };\n")
	}
	if usesForeign(p, "dequeueCode") {
		b.WriteString("const dequeueCode = () => q => () => { const a = __q.get(q.toString()); return (a && a.length) ? a.shift() : 0n; };\n")
	}
	// D3 machine floats (f64) + the BLAS dot kernel — pure host bodies (native
	// Number arithmetic); a Float is a JS number, distinct from the BigInt Nat.
	// `Float` is a foreign TYPE but survives erasure as ok/err's type argument, so it
	// needs a trivial body (it is runtime-irrelevant, like ch205's Pid).
	if usesForeign(p, "Float") {
		b.WriteString("const Float = () => null;\n")
	}
	if usesForeign(p, "fromNat") {
		b.WriteString("const fromNat = () => n => Number(n);\n")
	}
	if usesForeign(p, "rasterFill") {
		// scribe L4: font-rs signed-area rasterizer in native float64 — the
		// EXACT algorithm of goforge.dev/scribe raster.line (IEEE binary64,
		// no FMA in JS semantics, so it agrees with the Go engine bit for
		// bit on snapped inputs; ch564 locks it against the pure exact
		// rasterizer). Input coords are snapped-1/256 subpixels, bias 2^23.
		b.WriteString("const rasterFill = () => w0 => h0 => stream => { const w = Number(w0), h = Number(h0); const arr = []; let t = stream; while (t.tag === 1) { arr.push(Number(t.args[1])); t = t.args[2]; } const BIAS = 8388608, stride = w + 1; const acc = new Float64Array(stride * h); let i = 0; const nPolys = arr[i++]; const line = (p0x, p0y, p1x, p1y) => { if (p0y === p1y) return; let dir = 1; if (p0y > p1y) { dir = -1; const tx = p0x, ty = p0y; p0x = p1x; p0y = p1y; p1x = tx; p1y = ty; } const dxdy = (p1x - p0x) / (p1y - p0y); let x = p0x, yStart = p0y; if (yStart < 0) { x = p0x + (-p0y) * dxdy; yStart = 0; } let yEnd = p1y; if (yEnd > h) yEnd = h; if (yStart >= yEnd) return; const y0i = Math.floor(yStart), y1i = Math.ceil(yEnd); for (let y = y0i; y < y1i; y++) { const rowTop = Math.max(y, yStart), rowBot = Math.min(y + 1, yEnd); const dy = rowBot - rowTop; const xNext = x + dxdy * dy; const d = dy * dir; let xa = x, xb = xNext; if (xa > xb) { const tt = xa; xa = xb; xb = tt; } if (xa < 0) xa = 0; if (xb < 0) xb = 0; if (xa > w) xa = w; if (xb > w) xb = w; const row = y * stride; const x0f = Math.floor(xa), x0i = x0f | 0; const x1c = Math.ceil(xb), x1i = x1c | 0; if (x1i <= x0i + 1) { const xmf = 0.5 * (xa + xb) - x0f; acc[row + x0i] += d * (1 - xmf); if (x0i + 1 < stride) acc[row + x0i + 1] += d * xmf; } else { const s = 1 / (xb - xa); const x0fr = xa - x0f; const a0 = 0.5 * s * (1 - x0fr) * (1 - x0fr); const x1fr = xb - x1c + 1; const am = 0.5 * s * x1fr * x1fr; acc[row + x0i] += d * a0; if (x1i === x0i + 2) { acc[row + x0i + 1] += d * (1 - a0 - am); } else { const a1 = s * (1.5 - x0fr); acc[row + x0i + 1] += d * (a1 - a0); for (let xi = x0i + 2; xi < x1i - 1; xi++) acc[row + xi] += d * s; const a2 = a1 + (x1i - x0i - 3) * s; acc[row + x1i - 1] += d * (1 - a2 - am); } if (x1i < stride) acc[row + x1i] += d * am; } x = xNext; } }; for (let pI = 0; pI < nPolys; pI++) { const n = arr[i++]; const pts = []; for (let k = 0; k < n; k++) { pts.push([(arr[i++] - BIAS) / 256, (arr[i++] - BIAS) / 256]); } for (let k = 0; k < n; k++) { const a = pts[k], bb = pts[(k + 1) % n]; line(a[0], a[1], bb[0], bb[1]); } } let code = 1n; const out = []; for (let y = 0; y < h; y++) { let a = 0; for (let x = 0; x < w; x++) { a += acc[y * stride + x]; let cov = Math.abs(a); if (cov > 1) cov = 1; let al = Math.floor(cov * 256); if (al > 255) al = 255; out.push(al); } } for (let k = out.length - 1; k >= 0; k--) code = code * 256n + BigInt(out[k]); return code; };\n")
	}
	if usesForeign(p, "fadd") {
		b.WriteString("const fadd = () => a => b => a + b;\n")
	}
	if usesForeign(p, "fsub") {
		b.WriteString("const fsub = () => a => b => a - b;\n")
	}
	if usesForeign(p, "fmul") {
		b.WriteString("const fmul = () => a => b => a * b;\n")
	}
	if usesForeign(p, "fdiv") {
		b.WriteString("const fdiv = () => a => b => a / b;\n")
	}
	if usesForeign(p, "fabsP") {
		b.WriteString("const fabsP = () => x => Math.abs(x);\n")
	}
	if usesForeign(p, "floatToNat") {
		b.WriteString("const floatToNat = () => x => BigInt(Math.trunc(x));\n")
	}
	if usesForeign(p, "fleqN") {
		b.WriteString("const fleqN = () => a => b => (a <= b ? 1n : 0n);\n")
	}
	if usesForeign(p, "fsqrt") {
		b.WriteString("const fsqrt = () => x => Math.sqrt(x);\n")
	}
	if usesForeign(p, "fpow") {
		b.WriteString("const fpow = () => b => e => Math.pow(b, e);\n")
	}
	// Float IO: parseFloat/getFloat/printFloat — the standard IO vocabulary for f64.
	// __fmtf: canonical ECMAScript Number::toString with -0 mapped to "0".
	// __parsef: strict regex accept (no nan/inf/spaces/hex); null on reject.
	if usesForeign(p, "parseFloat") || usesForeign(p, "getFloat") || usesForeign(p, "printFloat") {
		b.WriteString("const __fmtf = x => Object.is(x, -0) ? \"0\" : String(x);\n")
		b.WriteString("const __parsef = s => /^[+-]?((\\d+(\\.\\d*)?)|(\\.\\d+))([eE][+-]?\\d+)?$/.test(s) ? Number(s) : null;\n")
	}
	if usesForeign(p, "parseFloat") {
		// parseFloat takes a packed-Nat string code; __s2h decodes it, __parsef tests it.
		b.WriteString("const parseFloat = () => s => { const v = __parsef(__s2h(s)); return v === null ? {tag:0,name:\"none\",args:[null]} : {tag:1,name:\"some\",args:[null,v]}; };\n")
	}
	if usesForeign(p, "getFloat") {
		if usesOTP(p) || usesLiveKV(p) || usesNet(p) || usesTLS(p) {
			b.WriteString("const getFloat = () => async () => { const fs = await import('node:fs'); const v = __parsef(fs.readFileSync(0, 'utf8').split('\\n')[0].replace(/\\r+$/, '')); return v === null ? 0.0 : v; };\n")
		} else {
			b.WriteString("const getFloat = () => () => { const v = __parsef(require('fs').readFileSync(0, 'utf8').split('\\n')[0].replace(/\\r+$/, '')); return v === null ? 0.0 : v; };\n")
		}
	}
	if usesForeign(p, "printFloat") {
		b.WriteString("const printFloat = () => x => () => { console.log(__fmtf(x)); return x; };\n")
	}
	if usesForeign(p, "dot2") {
		b.WriteString("const dot2 = () => a0 => a1 => b0 => b1 => a0 * b0 + a1 * b1;\n")
	}
	// Arbitrary-length dot: walk two Rune FLists (fcons = tag 1, head in args[0], tail
	// in args[1]) in lockstep summing products. The native backends route this through
	// cblas_ddot; on the source backends it is the portable reference loop.
	if usesForeign(p, "dotList") {
		b.WriteString("const dotList = () => xs => ys => { let s = 0; while (xs.tag === 1 && ys.tag === 1) { s += xs.args[0] * ys.args[0]; xs = xs.args[1]; ys = ys.args[1]; } return s; };\n")
	}
	// D4 interop: npDot is the uniform dot CAPABILITY (NumPy on py, OpenBLAS on C/LLVM);
	// on the other source backends it is the portable reference floor.
	if usesForeign(p, "npDot") {
		b.WriteString("const npDot = () => xs => ys => { let s = 0; while (xs.tag === 1 && ys.tag === 1) { s += xs.args[0] * ys.args[0]; xs = xs.args[1]; ys = ys.args[1]; } return s; };\n")
	}
	// D4 interop: npMean — the reference floor (hand sum/count); py serves real numpy.mean.
	if usesForeign(p, "npMean") {
		b.WriteString("const npMean = () => xs => { let s = 0, n = 0; while (xs.tag === 1) { s += xs.args[0]; n++; xs = xs.args[1]; } return n > 0 ? s / n : 0; };\n")
	}
	// Matrix product sum: flat row-major A (m×k) and B (k×n) as Rune FLists; sum all
	// entries of A·B. The native backends route this through cblas_dgemm; on the source
	// backends it is the portable triple-loop reference.
	if usesForeign(p, "gemmSum") {
		b.WriteString("const gemmSum = () => m => k => n => A => B => { const a = [], b2 = []; for (let t = A; t.tag === 1; t = t.args[1]) a.push(t.args[0]); for (let t = B; t.tag === 1; t = t.args[1]) b2.push(t.args[0]); const M = Number(m), K = Number(k), N = Number(n); let s = 0; for (let i = 0; i < M; i++) for (let j = 0; j < N; j++) for (let l = 0; l < K; l++) s += a[i * K + l] * b2[l * N + j]; return s; };\n")
	}
	// D4 interop: npNorm — the sqrt(sum of squares) floor; py serves numpy.linalg.norm.
	if usesForeign(p, "npNorm") {
		b.WriteString("const npNorm = () => xs => { let s = 0; while (xs.tag === 1) { s += xs.args[0] * xs.args[0]; xs = xs.args[1]; } return Math.sqrt(s); };\n")
	}
	// D4 interop: npMax — the fold-max reference floor (base 0); py serves real numpy.max.
	if usesForeign(p, "npMax") {
		b.WriteString("const npMax = () => xs => { let m = 0, t = xs, first = true; while (t.tag === 1) { if (first || t.args[0] > m) { m = t.args[0]; first = false; } t = t.args[1]; } return m; };\n")
	}
	// D4 interop: npVar — the 2-pass reference floor (mean, then mean of sq deviations).
	if usesForeign(p, "npVar") {
		b.WriteString("const npVar = () => xs => { let s = 0, n = 0, t = xs; while (t.tag === 1) { s += t.args[0]; n++; t = t.args[1]; } const m = n > 0 ? s / n : 0; let v = 0, u = xs; while (u.tag === 1) { const d = u.args[0] - m; v += d * d; u = u.args[1]; } return n > 0 ? v / n : 0; };\n")
	}
	// D4 interop: npMatSum — the triple-loop reference floor; py serves real numpy matmul.
	if usesForeign(p, "npMatSum") {
		b.WriteString("const npMatSum = () => m => k => n => A => B => { const a = [], b2 = []; for (let t = A; t.tag === 1; t = t.args[1]) a.push(t.args[0]); for (let t = B; t.tag === 1; t = t.args[1]) b2.push(t.args[0]); const M = Number(m), K = Number(k), N = Number(n); let s = 0; for (let i = 0; i < M; i++) for (let j = 0; j < N; j++) for (let l = 0; l < K; l++) s += a[i * K + l] * b2[l * N + j]; return s; };\n")
	}
	for _, d := range p.Datas {
		if p.Nat != nil && d.ElimName == p.Nat.ElimName {
			emitNat(&b, *p.Nat)
			continue
		}
		for _, c := range d.Ctors {
			emitCtor(&b, c)
		}
		// B2 / R-IR: the eliminator is lowered to the shared IR once (LowerElim)
		// and emitted as an ordinary definition — no eliminator-specific backend
		// code beyond rendering ICase/IField.
		fmt.Fprintf(&b, "const %s = %s;\n", jsName(d.ElimName), em.expr(LowerElim(d), nil))
	}
	if len(p.Partials) > 0 {
		b.WriteString(trampolineRuntime)
	}
	for _, def := range p.Defs {
		if p.Partials[def.Name] {
			// T2: a partial emits in two parts so deep tail recursion runs flat.
			//   <name>_step — the body; a marked tail call (IBounce) evaluates to a
			//                 bounce {$b: thunk} that re-enters _step, so no frame nests.
			//   <name>      — the public entry: apply _step to the params, then drive
			//                 the bounce chain once ($tramp). Callers see a value.
			step := jsName(def.Name) + "_step"
			fmt.Fprintf(&b, "const %s = %s;\n", step, em.expr(def.Body, nil))
			fmt.Fprintf(&b, "const %s = %s;\n", jsName(def.Name), em.exprPartialPublic(def.Body, step, nil))
			continue
		}
		fmt.Fprintf(&b, "const %s = %s;\n", jsName(def.Name), em.expr(def.Body, nil))
	}

	if p.Main != "" {
		b.WriteString(showRuntime)
		mainExpr := jsName(p.Main)
		if p.IOMain {
			// An IO main is a world thunk: run it (force) before showing.
			mainExpr += "()"
		}
		if p.IOMain && usesOTP(p) {
			// OTP's IO is async (the thunk returns a Promise): run main inside its own
			// mailbox context (so primSelf/primReceive work for main too), then await.
			thunk := jsName(p.Main) + "()"
			fmt.Fprintf(&b, "(async () => { console.log($show(await __als.run(__mkbox(), () => %s))); })();\n", thunk)
		} else if p.IOMain && (usesLiveKV(p) || usesNet(p) || usesTLS(p)) {
			// live-kv / socket / TLS IO is async (socket round-trips); await main before showing.
			fmt.Fprintf(&b, "(async () => { console.log($show(await %s())); })();\n", jsName(p.Main))
		} else {
			fmt.Fprintf(&b, "console.log($show(%s));\n", mainExpr)
		}
	}
	return TargetSource(b.String()), nil
}

// ioRuntime is the erased IO monad (R-EFFECT / C3): IO A is a deferred thunk
// `() => A`; pureIO returns a constant thunk; bindIO sequences — run m, feed its
// result to k, run that. Type arguments arrive as units. World/IO erase to units.
const ioRuntime = `const pureIO = A => a => () => a;
const bindIO = A => B => m => k => () => k(m())();
`

// ioRuntimeAsync is the IO monad for OTP programs (D5): IO A is a thunk returning a
// Promise, so bindIO AWAITS each step — primReceive can block (await a message). `await`
// of a plain value is the value, so the synchronous foreign prims compose unchanged.
const ioRuntimeAsync = `const pureIO = A => a => () => a;
const bindIO = A => B => m => k => () => (async () => { const v = await m(); return await (k(v)()); })();
`

// jsOTPRuntime is the LIVE OTP runtime for the JS backend (D5 / R-OTP) — a single-
// threaded COOPERATIVE scheduler on the event loop (the R-OTP "JS microtask" shim). A
// Pid is a mailbox {q: queue, w: receive-waiters, done: exit-waiters, dead}. Actor-
// local identity (whose mailbox primSelf/primReceive read, ACROSS awaits) is carried by
// Node's AsyncLocalStorage — the JS analogue of the Go goroutine-id / JVM ThreadLocal.
const jsOTPRuntime = `import { AsyncLocalStorage } from 'node:async_hooks';
const __als = new AsyncLocalStorage();
const __mkbox = () => ({ q: [], w: [], done: [], dead: false });
const __self = () => __als.getStore();
const __mark = box => { if (!box.dead) { box.dead = true; box.done.forEach(r => r()); box.done = []; } };
const Pid = () => _M => $unit;
const primSelf = () => _M => () => __self();
const primSpawn = () => _M => b => () => { const box = __mkbox(); __als.run(box, () => { Promise.resolve(b(box)()).finally(() => __mark(box)); }); return box; };
const primSend = () => _M => p => x => () => { if (p.w.length) p.w.shift()(x); else p.q.push(x); return $unit; };
const primReceive = () => _M => () => new Promise(res => { const box = __self(); if (box.q.length) res(box.q.shift()); else box.w.push(res); });
const primExit = () => _M => p => () => { __mark(p); return $unit; };
const primMonitor = () => _M => p => () => new Promise(res => { if (p.dead) res($unit); else p.done.push(() => res($unit)); });
`

// jsNetRuntime is the Phase-1 uniform socket FFI for the JS backend. Node TCP is
// async, so each op returns a Promise the async IO monad (ioRuntimeAsync) awaits —
// the blocking sockConnect/Write/Read/Close surface is satisfied asynchronously,
// the same mechanism live-KV/OTP already use. Handles are Node socket objects;
// payloads are Bin (Arrays of byte Numbers); sockRead returns up to n bytes from one
// data event (empty on end).
const jsNetRuntime = `import * as __netS from 'node:net';
const __sockConnect = (host, port) => new Promise(res => { const s = __netS.connect(port, host, () => res(s)); s.on('error', () => res(null)); });
const __sockWrite = (s, data) => new Promise(res => { const buf = Buffer.from(data); s.write(buf, () => res(BigInt(buf.length))); });
const __sockRead = (s, n) => new Promise(res => { const onData = d => { s.pause(); s.removeListener('data', onData); s.removeListener('end', onEnd); res(Array.from(d.subarray(0, n))); }; const onEnd = () => { s.removeListener('data', onData); res([]); }; s.on('data', onData); s.on('end', onEnd); s.resume(); });
const __sockClose = s => new Promise(res => { if (s.close) s.close(); else s.end(); res($unit); });
const __sockListen = port => new Promise(res => { const srv = __netS.createServer(); srv.maxConnections = 1024; srv.listen(port, '127.0.0.1', () => res(srv)); srv.on('error', () => res(null)); });
const __sockAccept = srv => new Promise(res => { srv.once('connection', s => { s.pause(); res(s); }); });
const sockConnect = () => host => port => () => __sockConnect(Buffer.from(host).toString('latin1'), Number(port));
const sockWrite = () => s => data => () => __sockWrite(s, data);
const sockRead = () => s => n => () => __sockRead(s, Number(n));
const sockClose = () => s => () => __sockClose(s);
const sockListen = () => port => () => __sockListen(Number(port));
const sockAccept = () => srv => () => __sockAccept(srv);
`

// jsLiveKVRuntime is the E4 live-broker data-plane binding for the JS backend: kv +
// queue ops speak RESP over a Node socket to $WAVELET_KV_URL. Node sockets are async,
// so each op returns a Promise the async IO monad awaits (the Go/JVM bindings block
// instead). Hand-rolled RESP, no third-party dependency (node:net only).
const jsLiveKVRuntime = `import * as __net from 'node:net';
const __kvAddr = () => { let u = (process.env.WAVELET_KV_URL || '').replace('redis://', ''); if (!u) u = 'localhost:6379'; const ix = u.indexOf(':'); return { h: u.slice(0, ix), p: Number(u.slice(ix + 1)) }; };
const __resp = (...args) => new Promise(res => { const { h, p } = __kvAddr(); const sock = __net.connect(p, h, () => { let cmd = '*' + args.length + '\r\n'; for (const a of args) cmd += '$' + a.length + '\r\n' + a + '\r\n'; sock.write(cmd); }); let buf = ''; let done = false; const fin = v => { if (!done) { done = true; res(v); sock.end(); } }; sock.on('data', d => { buf += d.toString('latin1'); const i = buf.indexOf('\r\n'); if (i < 0) return; const line = buf.slice(0, i); if (line[0] === '+') fin(line.slice(1)); else if (line[0] === '$') { const n = parseInt(line.slice(1)); if (n < 0) { fin(''); return; } if (buf.length >= i + 2 + n) fin(buf.slice(i + 2, i + 2 + n)); } else fin(''); }); sock.on('error', () => fin('')); });
const kvSetLive = () => k => v => () => __resp('SET', __s2h(k), __s2h(v)).then(() => v);
const kvGetLive = () => k => () => __resp('GET', __s2h(k)).then(r => __h2s(r));
const enqueueLive = () => q => m => () => __resp('LPUSH', __s2h(q), __s2h(m)).then(() => m);
const dequeueLive = () => q => () => __resp('RPOP', __s2h(q)).then(r => __h2s(r));
`

// jsFrontierRuntime is the seeded cooperative scheduler for the par combinator
// (Task 6 / R-EFFECT), the JS twin of goFrontierRuntime. The 64-bit LCG runs on
// BigInt with a 0xFFFFFFFFFFFFFFFF mask so the wrapping arithmetic matches Go's
// uint64 exactly; under a fixed WAVELET_SEED (default 0) the ready-pick sequence
// is byte-identical on both backends (the cross-backend conformance contract).
//
// __frontier receives a pre-flattened array of IO action thunks (compile-time
// par-spine flattening by parDispatch/flattenParSpine; no erased-List decoding).
// Each action is an IO thunk `() => A`, forced by calling it; __frontier returns
// an IO-shaped thunk so it composes with the surrounding IO monad. The last
// forced result is returned (par : IO A -> IO B -> IO B).
const jsFrontierRuntime = `let __schedState = (() => { const s = (typeof process !== 'undefined' && process.env.WAVELET_SEED) || "0"; return BigInt(s) & 0xFFFFFFFFFFFFFFFFn; })();
const __schedNext = n => { __schedState = (__schedState * 6364136223846793005n + 1442695040888963407n) & 0xFFFFFFFFFFFFFFFFn; return Number(__schedState % BigInt(n)); };
const __frontier = xs => () => { let last; while (xs.length > 0) { const i = __schedNext(xs.length); last = xs[i](); xs.splice(i, 1); } return last; };
`

// parDispatch recognizes a saturated par-headed application spine and emits a
// compile-time-flattened __frontier call (the JS twin of the Go parDispatch).
// par : (A : U) -> (B : U) -> IO A -> IO B -> IO B; do-blocks desugar to
// right-nested par trees, flattened here into one __frontier over every IO
// action. Returns ("", false) when the node is not a saturated par.
func (em *jsEmitter) parDispatch(app IApp, env []string) (string, bool) {
	actions := em.flattenParSpine(Ir(app))
	if actions == nil {
		return "", false
	}
	parts := make([]string, len(actions))
	for i, a := range actions {
		parts[i] = em.expr(a, env)
	}
	return fmt.Sprintf("__frontier([%s])", strings.Join(parts, ", ")), true
}

// flattenParSpine walks a right-nested par spine and returns the ordered list of
// IO-action sub-terms (a1, a2, ..., an). A saturated par application has the
// shape IApp(IApp(IApp(IApp(IGlobal "par", _), _), action_left), rest) where rest
// may itself be a saturated par (right-nested) or a bare IO action (leaf).
// Returns nil when t is not a saturated par. Mirrors goEmitter.flattenParSpine
// exactly so both backends flatten to the same action order.
func (em *jsEmitter) flattenParSpine(t Ir) []Ir {
	var args []Ir
	cur := t
	for {
		app, ok := cur.(IApp)
		if !ok {
			break
		}
		args = append([]Ir{app.Arg}, args...)
		cur = app.Fn
	}
	g, ok := cur.(IGlobal)
	if !ok || g.Name != "par" || len(args) != 4 {
		return nil
	}
	left := args[2]
	right := args[3]
	if nested := em.flattenParSpine(right); nested != nil {
		return append([]Ir{left}, nested...)
	}
	return []Ir{left, right}
}

// usesIO reports whether any emitted definition references an IO builtin.
func usesIO(p Program) bool {
	names := map[string]bool{"pureIO": true, "bindIO": true}
	var walk func(Ir) bool
	walk = func(t Ir) bool {
		switch x := t.(type) {
		case IGlobal:
			return names[x.Name]
		case ILam:
			return walk(x.Body)
		case IApp:
			return walk(x.Fn) || walk(x.Arg)
		case ILet:
			return walk(x.Val) || walk(x.Body)
		case IPair:
			return walk(x.A) || walk(x.B)
		case IFst:
			return walk(x.P)
		case ISnd:
			return walk(x.P)
		default:
			return false
		}
	}
	for _, d := range p.Defs {
		if walk(d.Body) {
			return true
		}
	}
	return false
}

// emitCtor writes a curried constructor: parameters (erased) and arguments
// are all collected; the record keeps every argument so the eliminator can
// slice off the parameter prefix uniformly.
func emitCtor(b *strings.Builder, c CtorSpec) {
	fmt.Fprintf(b, "const %s = ", jsName(c.Name))
	for i := 0; i < c.Arity; i++ {
		fmt.Fprintf(b, "a%d => ", i)
	}
	args := make([]string, c.Arity)
	for i := range args {
		args[i] = fmt.Sprintf("a%d", i)
	}
	fmt.Fprintf(b, "({tag: %d, name: %q, args: [%s]});\n", c.Tag, c.Name, strings.Join(args, ", "))
}

// emitNat writes the builtin-nat group as BigInt arithmetic (rung 6): zero is
// 0n, succ adds one, and the eliminator is a loop — acc starts at the zero
// case and folds the succ case k times, exactly the iota-rule unrolled. The
// motive is erased and ignored.
func emitNat(b *strings.Builder, n NatSpec) {
	b.WriteString(natDispatchRuntime)
	fmt.Fprintf(b, "const %s = 0n;\n", jsName(n.Zero))
	fmt.Fprintf(b, "const %s = x => x + 1n;\n", jsName(n.Succ))
	fmt.Fprintf(b, "const %s = m => c0 => c1 => x => {\n", jsName(n.ElimName))
	b.WriteString("  let acc = c0;\n")
	b.WriteString("  for (let k = 0n; k < x; k++) acc = c1(k)(acc);\n")
	b.WriteString("  return acc;\n")
	b.WriteString("};\n")
}

// trampolineRuntime is the T2 driver: a bounce is the tagged thunk {$b: () => …}
// a marked tail call evaluates to; $tramp forces the chain until a real value
// surfaces. No real erased value carries a `$b` property (ctors are {tag,name,args},
// pairs are arrays, functions/BigInt/strings have no such field), so the marker is
// unambiguous. Emitted only when the program has a partial def.
const trampolineRuntime = `const $tramp = r => { while (r && r.$b !== undefined) r = r.$b(); return r; };
`

// exprBounce emits a marked tail self/sibling call as a bounce {$b: () => <head>_step(args…)}
// re-entering the head partial's _step entry (the body without a driver), so forcing
// it advances ONE iteration; the enclosing public driver loops without nesting.
func (em *jsEmitter) exprBounce(call Ir, env []string) string {
	var args []Ir
	t := call
	for {
		app, ok := t.(IApp)
		if !ok {
			break
		}
		args = append([]Ir{app.Arg}, args...)
		t = app.Fn
	}
	g, ok := t.(IGlobal)
	if !ok {
		return em.expr(call, env) // not a recognizable spine; emit directly
	}
	s := jsName(g.Name) + "_step"
	for _, a := range args {
		s = fmt.Sprintf("%s(%s)", s, em.expr(a, env))
	}
	return "({$b: () => " + s + "})"
}

// exprPartialPublic emits a partial def's public entry: peel its parameter lambdas,
// apply the _step thunk to them, and drive ($tramp) the result once.
func (em *jsEmitter) exprPartialPublic(t Ir, step string, env []string) string {
	var params []string
	cur := t
	for {
		lam, ok := cur.(ILam)
		if !ok {
			break
		}
		n := freshJS(lam.Name, env)
		params = append(params, n)
		env = prepend(n, env)
		cur = lam.Body
	}
	app := step
	for _, pn := range params {
		app = fmt.Sprintf("%s(%s)", app, pn)
	}
	out := "$tramp(" + app + ")"
	for i := len(params) - 1; i >= 0; i-- {
		out = fmt.Sprintf("%s => %s", params[i], out)
	}
	return out
}

// jsEmitter renders IR terms. It knows the builtin-nat eliminator's name so
// it can emit DISPATCH for case-shaped eliminations (rung 6): the BigInt
// eliminator is a strict fold, which evaluates the successor branch's body at
// every step — exponential when the body recurses but never uses the IH (a
// plain `case` on a number). A saturated nat-elim whose successor case
// provably ignores its induction hypothesis emits `$natD` instead: one
// zero-test, one branch-body evaluation, the unused IH replaced by $unit.
type jsEmitter struct {
	natElim string
	// accel maps a registered accel-op def name to its NatOp kind (C7 / R-NUM
	// Decision 4): a saturated 2-arg call to such a def emits native arithmetic.
	accel map[string]core.NatOp
}

// natDispatchRuntime is the constant-time case-on-nat (see jsEmitter).
const natDispatchRuntime = `const $natD = c0 => c1 => x => x === 0n ? c0 : c1(x - 1n)($unit);
`

func (em *jsEmitter) expr(t Ir, env []string) string {
	switch x := t.(type) {
	case IVar:
		if x.Idx < len(env) {
			return env[x.Idx]
		}
		return fmt.Sprintf("$v%d", x.Idx)
	case IGlobal:
		return jsName(x.Name)
	case IForeign:
		// Host-linked accessor: the host defines `function <primname>(){ return … }`.
		// primName strips any module qualifier (e.g. "Std.Float.getFloat" -> "getFloat")
		// so module-wrapped foreigns call the same host body as unqualified ones.
		return jsName(primName(x.Name)) + "()"
	case IUnit:
		return "$unit"
	case ILit:
		// Native host literal marshalled at the FFI boundary (B4): a JS number or
		// string literal. (Distinct from Peano Nat, which is a BigInt.)
		if x.Kind == LitNat {
			// C7 / R-NUM: a compressed numeral deploys as a BigInt literal — the
			// SAME runtime representation NatSpec gives nat (`0n`/`x + 1n`), so it
			// is interchangeable with a succ-chain but O(1) source. Plain `%d`
			// would emit a Number, which `x + 1n` rejects.
			return x.Nat + "n"
		}
		if x.Kind == LitStr {
			return fmt.Sprintf("%q", x.Str)
		}
		if x.Kind == LitPtr {
			// Opaque host pointer: boxed so it never aliases a number, and $show
			// renders it as a fixed marker rather than its handle (un-show-able).
			return fmt.Sprintf("({ptr: %d})", x.Int)
		}
		return fmt.Sprintf("%d", x.Int)
	case ILam:
		n := freshJS(x.Name, env)
		return fmt.Sprintf("(%s => %s)", n, em.expr(x.Body, prepend(n, env)))
	case IApp:
		if out, ok := em.accelDispatch(x, env); ok {
			return out
		}
		if out, ok := em.natDispatch(x, env); ok {
			return out
		}
		if out, ok := em.parDispatch(x, env); ok {
			return out
		}
		return fmt.Sprintf("%s(%s)", em.expr(x.Fn, env), em.expr(x.Arg, env))
	case ILet:
		n := freshJS(x.Name, env)
		return fmt.Sprintf("((%s) => %s)(%s)", n, em.expr(x.Body, prepend(n, env)), em.expr(x.Val, env))
	case IPair:
		return fmt.Sprintf("[%s, %s]", em.expr(x.A, env), em.expr(x.B, env))
	case IFst:
		return fmt.Sprintf("(%s)[0]", em.expr(x.P, env))
	case ISnd:
		return fmt.Sprintf("(%s)[1]", em.expr(x.P, env))
	case IField:
		return fmt.Sprintf("(%s).args[%d]", em.expr(x.Scrut, env), x.Index)
	case IBounce:
		return em.exprBounce(x.Call, env)
	case ICase:
		var b strings.Builder
		fmt.Fprintf(&b, "(() => { switch ((%s).tag) {", em.expr(x.Scrut, env))
		for _, arm := range x.Arms {
			fmt.Fprintf(&b, " case %d: return %s;", arm.Tag, em.expr(arm.Body, env))
		}
		b.WriteString(" } throw new Error('impossible: unmatched constructor tag'); })()")
		return b.String()
	default:
		panic("codegen: unknown IR node")
	}
}

// accelDispatch emits a registered accel-op def applied to two args as native
// BigInt arithmetic (C7 / R-NUM Decision 4): add → `(a + b)`, mul → `(a * b)`,
// monus → saturating `(a > b ? a - b : 0n)` (Peano monus floors at 0, never
// negative). The args use the SAME BigInt representation NatSpec gives nat, so
// the result is interchangeable with the eliminator-loop value but O(1).
func (em *jsEmitter) accelDispatch(app IApp, env []string) (string, bool) {
	op, a, b, ok := accelMatch(app, em.accel)
	if !ok {
		return "", false
	}
	ea, eb := em.expr(a, env), em.expr(b, env)
	switch op {
	case core.NatOpAdd:
		return fmt.Sprintf("(%s + %s)", ea, eb), true
	case core.NatOpMul:
		return fmt.Sprintf("(%s * %s)", ea, eb), true
	case core.NatOpMonus:
		return fmt.Sprintf("(() => { const $a = %s, $b = %s; return $a > $b ? $a - $b : 0n; })()", ea, eb), true
	case core.NatOpDiv:
		return fmt.Sprintf("(() => { const $a = %s, $b = %s; return $b === 0n ? 0n : $a / $b; })()", ea, eb), true
	case core.NatOpMod:
		return fmt.Sprintf("(() => { const $a = %s, $b = %s; return $b === 0n ? $a : $a %% $b; })()", ea, eb), true
	}
	return "", false
}

// natDispatch recognizes `NatElim m c0 c1 x [extra...]` where c1 is a literal
// two-binder lambda whose body never uses the second binder (the IH), and
// emits the constant-time dispatch.
func (em *jsEmitter) natDispatch(app IApp, env []string) (string, bool) {
	if em.natElim == "" {
		return "", false
	}
	var args []Ir
	t := Ir(app)
	for {
		a, ok := t.(IApp)
		if !ok {
			break
		}
		args = append([]Ir{a.Arg}, args...)
		t = a.Fn
	}
	g, ok := t.(IGlobal)
	if !ok || g.Name != em.natElim || len(args) < 4 {
		return "", false
	}
	c1lam, ok := args[2].(ILam)
	if !ok {
		return "", false
	}
	inner, ok := c1lam.Body.(ILam)
	if !ok || usesVar(inner.Body, 0) {
		return "", false
	}
	out := fmt.Sprintf("$natD(%s)(%s)(%s)",
		em.expr(args[1], env), em.expr(args[2], env), em.expr(args[3], env))
	for _, extra := range args[4:] {
		out = fmt.Sprintf("%s(%s)", out, em.expr(extra, env))
	}
	return out, true
}

// usesVar reports whether the de Bruijn index occurs in t.
func usesVar(t Ir, idx int) bool {
	switch x := t.(type) {
	case IVar:
		return x.Idx == idx
	case ILam:
		return usesVar(x.Body, idx+1)
	case IApp:
		return usesVar(x.Fn, idx) || usesVar(x.Arg, idx)
	case ILet:
		return usesVar(x.Val, idx) || usesVar(x.Body, idx+1)
	default:
		return false
	}
}

func prepend(n string, env []string) []string {
	out := make([]string, 0, len(env)+1)
	out = append(out, n)
	return append(out, env...)
}

// jsOpNames maps the operator definition names of GRAMMAR §3 (symbolic
// identifiers, invalid in JS) to mangled JS identifiers.
var jsOpNames = map[string]string{
	"+":  "$add",
	"-":  "$sub",
	"*":  "$mul",
	"/":  "$div",
	"//": "$fdiv",
	"%":  "$mod",
	"++": "$append",
}

// jsName sanitizes a rune identifier into a JS one. Alphabetic rune identifiers
// are already close (letters, digits, _, '); primes become $. Operator names
// mangle through jsOpNames.
func jsName(n string) string {
	if m, ok := jsOpNames[n]; ok {
		return m
	}
	// A shadow-suffixed operator ("+$<hash>", from the session's per-hash
	// emit names) mangles its operator head and keeps the suffix.
	if i := strings.IndexByte(n, '$'); i > 0 {
		if m, ok := jsOpNames[n[:i]]; ok {
			return m + n[i:]
		}
	}
	n = strings.ReplaceAll(n, "'", "$")
	if n == "" {
		n = "$x"
	}
	if jsReserved[n] {
		return "$" + n
	}
	return n
}

func freshJS(hint string, env []string) string {
	n := jsName(hint)
	if n == "" || n == "_" {
		n = "$x"
	}
	for {
		clash := false
		for _, e := range env {
			if e == n {
				clash = true
				break
			}
		}
		if !clash {
			return n
		}
		n += "$"
	}
}

var jsReserved = map[string]bool{
	"await": true, "break": true, "case": true, "catch": true, "class": true,
	"const": true, "continue": true, "debugger": true, "default": true,
	"delete": true, "do": true, "else": true, "enum": true, "export": true,
	"extends": true, "false": true, "finally": true, "for": true,
	"function": true, "if": true, "import": true, "in": true,
	"instanceof": true, "new": true, "null": true, "return": true,
	"super": true, "switch": true, "this": true, "throw": true, "true": true,
	"try": true, "typeof": true, "var": true, "void": true, "while": true,
	"with": true, "yield": true, "let": true, "static": true,
}

// quotRuntime is the erased quotient builtin group (v2): a quotient compiles
// to its carrier, so qin is the identity on its point, qlift applies the
// lifted function directly, and the proof-valued members (qsound, qind) are
// unit-producers. Type and proof arguments arrive as units (no arity surgery).
const quotRuntime = `const qin = A => R => a => a;
const qsound = A => R => a => b => r => $unit;
const qlift = A => R => B => f => resp => q => f(q);
const qind = A => R => P => h => q => $unit;
`

// usesQuot reports whether any emitted definition references a quotient
// builtin by name, so the runtime is included only when needed.
func usesQuot(p Program) bool {
	names := map[string]bool{"qin": true, "qsound": true, "qlift": true, "qind": true}
	var walk func(Ir) bool
	walk = func(t Ir) bool {
		switch x := t.(type) {
		case IGlobal:
			return names[x.Name]
		case ILam:
			return walk(x.Body)
		case IApp:
			return walk(x.Fn) || walk(x.Arg)
		case ILet:
			return walk(x.Val) || walk(x.Body)
		default:
			return false
		}
	}
	for _, d := range p.Defs {
		if walk(d.Body) {
			return true
		}
	}
	return false
}

// showRuntime renders constructor values back in surface style for the
// program's printed result.
const showRuntime = `function $show(v) {
  if (v === null) return "()";
  if (typeof v === "function") return "<function>";
  if (typeof v === "bigint") return v.toString();
  if (v && typeof v === "object" && "ptr" in v) return "<ptr>";
  if (v && typeof v === "object" && "tag" in v) {
    let s = v.name;
    for (const a of v.args) {
      if (a === null) continue;
      const r = $show(a);
      s += " " + (r.includes(" ") ? "(" + r + ")" : r);
    }
    return s;
  }
  return String(v);
}
`
