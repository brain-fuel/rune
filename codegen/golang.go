package codegen

import (
	"fmt"
	"strings"

	"goforge.dev/rune/v3/core"
)

// Go is a third backend (B-track / multi-backend): plain Go over the SAME erased
// IR as JS/Python. Every value is `any`; a closure is `func(any) any`, applied
// through the `ap` helper (so the emit needs no inline type assertions);
// constructors build map records; eliminators are funcs with statement-body tag
// dispatch; nat is int; the unit token is nil. Each top-level definition becomes
// a thunk `name_() any` and references call it — this sidesteps Go's package-var
// initialization cycles for (mutually) recursive definitions and eliminators.
// Shadow-rule safe: a backend bug is wrong runtime output, never an unsound proof.
type Go struct{}

func (Go) Target() string { return "go" }

// Emit lowers a program to a self-contained Go source file (go run-able).
func (Go) Emit(p Program) (TargetSource, error) {
	em := &goEmitter{}
	if p.Nat != nil {
		em.natElim = p.Nat.ElimName
		em.accel = p.Nat.Ops
	}
	var b strings.Builder
	b.WriteString("// Generated by rune emit (go backend) — the erased shadow. Do not edit.\n")
	b.WriteString("package main\n\nimport (\n\t\"fmt\"\n\t\"strings\"\n)\n\n")
	b.WriteString(goRuntime)
	if usesQuot(p) {
		b.WriteString(goQuotRuntime)
	}
	if usesIO(p) {
		b.WriteString(goIORuntime)
	}
	for _, d := range p.Datas {
		if p.Nat != nil && d.ElimName == p.Nat.ElimName {
			emitNatGo(&b, *p.Nat)
			continue
		}
		for _, c := range d.Ctors {
			emitCtorGo(&b, c)
		}
		// B2 / R-IR: the eliminator is lowered to the shared IR once (LowerElim)
		// and emitted as an ordinary thunk — no eliminator-specific backend code.
		fmt.Fprintf(&b, "func %s() any { return %s }\n", goThunk(d.ElimName), em.expr(LowerElim(d), nil))
	}
	for _, def := range p.Defs {
		fmt.Fprintf(&b, "func %s() any { return %s }\n", goThunk(def.Name), em.expr(def.Body, nil))
	}
	b.WriteString("func main() {\n")
	if p.Main != "" {
		mainExpr := goThunk(p.Main) + "()"
		if p.IOMain {
			// An IO main is a deferred world thunk (unit -> A): force by applying nil.
			mainExpr = fmt.Sprintf("ap(%s, nil)", mainExpr)
		}
		fmt.Fprintf(&b, "\tfmt.Println(_show(%s))\n", mainExpr)
	}
	b.WriteString("}\n")
	return TargetSource(b.String()), nil
}

func emitCtorGo(b *strings.Builder, c CtorSpec) {
	fmt.Fprintf(b, "func %s() any { return ", goThunk(c.Name))
	for i := 0; i < c.Arity; i++ {
		fmt.Fprintf(b, "func(a%d any) any { return ", i)
	}
	args := make([]string, c.Arity)
	for i := range args {
		args[i] = fmt.Sprintf("a%d", i)
	}
	fmt.Fprintf(b, "map[string]any{\"tag\": %d, \"name\": %q, \"args\": []any{%s}}", c.Tag, c.Name, strings.Join(args, ", "))
	b.WriteString(strings.Repeat(" }", c.Arity))
	b.WriteString(" }\n")
}

func emitNatGo(b *strings.Builder, n NatSpec) {
	fmt.Fprintf(b, "func %s() any { return 0 }\n", goThunk(n.Zero))
	fmt.Fprintf(b, "func %s() any { return func(x any) any { return x.(int) + 1 } }\n", goThunk(n.Succ))
	fmt.Fprintf(b, "func %s() any { return func(m any) any { return func(c0 any) any { return func(c1 any) any { return func(x any) any { return _natElim(c0, c1, x) } } } } }\n", goThunk(n.ElimName))
}

type goEmitter struct {
	natElim string
	accel   map[string]core.NatOp
}

func (em *goEmitter) expr(t Ir, env []string) string {
	switch x := t.(type) {
	case IVar:
		if x.Idx < len(env) {
			return env[x.Idx]
		}
		return fmt.Sprintf("_v%d", x.Idx)
	case IGlobal:
		return goThunk(x.Name) + "()"
	case IForeign:
		// Host-linked accessor: the host defines `func <name>() any { return … }`.
		// A plain name (no _d suffix) keeps the FFI ABI distinct from def thunks.
		return goLocal(x.Name) + "()"
	case IUnit:
		return "nil"
	case ILit:
		// Native host literal marshalled at the FFI boundary (B4): a Go int or
		// string literal (both flow as `any`).
		if x.Kind == LitNat {
			// C7 / R-NUM: a compressed numeral deploys as a native Go int (boxed in
			// `any`) — the SAME representation NatSpec gives nat (`0`, `x.(int)+1`),
			// O(1) source instead of a succ-chain. Machine-bounded, matching the
			// succ-chain-to-int path it replaces.
			return fmt.Sprintf("any(%s)", x.Nat)
		}
		if x.Kind == LitStr {
			return fmt.Sprintf("any(%q)", x.Str)
		}
		if x.Kind == LitPtr {
			// Opaque host pointer: a distinct named type so it never aliases an
			// int, and _show renders it as a fixed marker rather than its handle.
			return fmt.Sprintf("any(_ptr(%d))", x.Int)
		}
		return fmt.Sprintf("any(%d)", x.Int)
	case ILam:
		n := freshGo(x.Name, env)
		return fmt.Sprintf("func(%s any) any { return %s }", n, em.expr(x.Body, prepend(n, env)))
	case IApp:
		if out, ok := em.accelDispatch(x, env); ok {
			return out
		}
		if out, ok := em.natDispatch(x, env); ok {
			return out
		}
		return fmt.Sprintf("ap(%s, %s)", em.expr(x.Fn, env), em.expr(x.Arg, env))
	case ILet:
		n := freshGo(x.Name, env)
		return fmt.Sprintf("func(%s any) any { return %s }(%s)", n, em.expr(x.Body, prepend(n, env)), em.expr(x.Val, env))
	case IPair:
		return fmt.Sprintf("[]any{%s, %s}", em.expr(x.A, env), em.expr(x.B, env))
	case IFst:
		return fmt.Sprintf("(%s).([]any)[0]", em.expr(x.P, env))
	case ISnd:
		return fmt.Sprintf("(%s).([]any)[1]", em.expr(x.P, env))
	case IField:
		return fmt.Sprintf("(%s).(map[string]any)[\"args\"].([]any)[%d]", em.expr(x.Scrut, env), x.Index)
	case ICase:
		// IIFE over a statement switch on the scrutinee tag.
		scrut := em.expr(x.Scrut, env)
		var b strings.Builder
		fmt.Fprintf(&b, "func() any { switch (%s).(map[string]any)[\"tag\"].(int) {", scrut)
		for _, arm := range x.Arms {
			fmt.Fprintf(&b, " case %d: return %s;", arm.Tag, em.expr(arm.Body, env))
		}
		b.WriteString(" }; return _impossible() }()")
		return b.String()
	default:
		panic("codegen(go): unknown IR node")
	}
}

// accelDispatch emits a registered accel-op def on two args as native int
// arithmetic (C7 / R-NUM Decision 4) through the `_natAdd`/`_natMul`/`_natMonus`
// runtime helpers (which unbox the `any`-wrapped ints, compute, and rebox).
// Monus is the saturating Peano monus (floors at 0). The int representation is
// the SAME NatSpec gives nat (`0`, `x.(int)+1`), so the result is interchangeable.
func (em *goEmitter) accelDispatch(app IApp, env []string) (string, bool) {
	op, a, b, ok := accelMatch(app, em.accel)
	if !ok {
		return "", false
	}
	ea, eb := em.expr(a, env), em.expr(b, env)
	switch op {
	case core.NatOpAdd:
		return fmt.Sprintf("_natAdd(%s, %s)", ea, eb), true
	case core.NatOpMul:
		return fmt.Sprintf("_natMul(%s, %s)", ea, eb), true
	case core.NatOpMonus:
		return fmt.Sprintf("_natMonus(%s, %s)", ea, eb), true
	}
	return "", false
}

func (em *goEmitter) natDispatch(app IApp, env []string) (string, bool) {
	if em.natElim == "" {
		return "", false
	}
	var args []Ir
	t := Ir(app)
	for {
		a, ok := t.(IApp)
		if !ok {
			break
		}
		args = append([]Ir{a.Arg}, args...)
		t = a.Fn
	}
	g, ok := t.(IGlobal)
	if !ok || g.Name != em.natElim || len(args) < 4 {
		return "", false
	}
	c1lam, ok := args[2].(ILam)
	if !ok {
		return "", false
	}
	inner, ok := c1lam.Body.(ILam)
	if !ok || usesVar(inner.Body, 0) {
		return "", false
	}
	out := fmt.Sprintf("_natD(%s, %s, %s)",
		em.expr(args[1], env), em.expr(args[2], env), em.expr(args[3], env))
	for _, extra := range args[4:] {
		out = fmt.Sprintf("ap(%s, %s)", out, em.expr(extra, env))
	}
	return out, true
}

func freshGo(hint string, env []string) string {
	n := goLocal(hint)
	for {
		clash := false
		for _, e := range env {
			if e == n {
				clash = true
				break
			}
		}
		if !clash {
			return n
		}
		n += "_"
	}
}

// goThunk is the package-level thunk name for a definition (suffix _ avoids
// clashing with Go keywords/builtins and the runtime helpers).
func goThunk(n string) string { return goLocal(n) + "_d" }

func goLocal(n string) string {
	if m, ok := jsOpNames[n]; ok {
		return m
	}
	if i := strings.IndexByte(n, '$'); i > 0 {
		if m, ok := jsOpNames[n[:i]]; ok {
			n = m + n[i:]
		}
	}
	n = strings.ReplaceAll(n, "'", "_")
	n = strings.ReplaceAll(n, "$", "_")
	n = strings.ReplaceAll(n, ".", "_")
	if n == "" {
		n = "_x"
	}
	// leading digit or empty → prefix.
	if c := n[0]; c >= '0' && c <= '9' {
		n = "_" + n
	}
	return n
}

const goRuntime = `type _ptr int64 // opaque host pointer (B4): never shown structurally
func ap(f, x any) any { return f.(func(any) any)(x) }
func _natElim(c0, c1, x any) any {
	acc := c0
	for k := 0; k < x.(int); k++ {
		acc = ap(ap(c1, k), acc)
	}
	return acc
}
func _natD(c0, c1, x any) any {
	if x.(int) == 0 {
		return c0
	}
	return ap(ap(c1, x.(int)-1), nil)
}
func _natAdd(a, b any) any { return any(a.(int) + b.(int)) }
func _natMul(a, b any) any { return any(a.(int) * b.(int)) }
func _natMonus(a, b any) any {
	x, y := a.(int), b.(int)
	if x > y {
		return any(x - y)
	}
	return any(0)
}
func _impossible() any { panic("impossible: unmatched constructor tag") }
func _show(v any) string {
	switch t := v.(type) {
	case nil:
		return "()"
	case _ptr:
		return "<ptr>"
	case int:
		return fmt.Sprintf("%d", t)
	case string:
		return t
	case bool:
		if t {
			return "true"
		}
		return "false"
	case []any:
		parts := make([]string, len(t))
		for i, a := range t {
			parts[i] = _show(a)
		}
		return "(" + strings.Join(parts, ", ") + ")"
	case map[string]any:
		s := t["name"].(string)
		for _, a := range t["args"].([]any) {
			if a == nil {
				continue
			}
			r := _show(a)
			if strings.Contains(r, " ") {
				r = "(" + r + ")"
			}
			s += " " + r
		}
		return s
	default:
		return fmt.Sprintf("%v", t)
	}
}
`

// goIORuntime is the erased IO monad (R-EFFECT / C3): IO A is a deferred thunk
// encoded as a unit-argument closure, so `ap` forces it by applying nil; pureIO
// returns a constant thunk, bindIO sequences. Type arguments arrive as units.
const goIORuntime = `func pureIO_d() any { return func(_A any) any { return func(a any) any { return func(_u any) any { return a } } } }
func bindIO_d() any { return func(_A any) any { return func(_B any) any { return func(m any) any { return func(k any) any { return func(_u any) any { return ap(ap(k, ap(m, nil)), nil) } } } } } }
`

const goQuotRuntime = `func qin_d() any { return func(a any) any { return func(r any) any { return func(x any) any { return x } } } }
func qsound_d() any { return func(a any) any { return func(r any) any { return func(x any) any { return func(y any) any { return func(p any) any { return nil } } } } } }
func qlift_d() any { return func(a any) any { return func(r any) any { return func(b any) any { return func(f any) any { return func(resp any) any { return func(q any) any { return ap(f, q) } } } } } } }
func qind_d() any { return func(a any) any { return func(r any) any { return func(p any) any { return func(h any) any { return func(q any) any { return nil } } } } } }
`
