package codegen

import (
	"fmt"
	"strings"

	"goforge.dev/rune/v3/core"
)

// Beam is the fifth backend (B-track / B3 — BEAM to genuine working) over the SAME
// erased IR: Erlang source run under escript. A runtime value is a closure (an
// Erlang `fun`), a machine integer (nat), the `unit` atom, a pair `{pair,A,B}`, or a
// tagged constructor `{c,Tag,Name,Args}`; application goes through the `ap/2` helper
// (so the emit needs no parenthesized-fun-call syntax). The eliminator is rendered
// from the SHARED LowerElim IR (ICase → an Erlang `case` on the tag, IField →
// `lists:nth`/`element`), so this backend carries NO eliminator-specific code — the
// B2 keystone is what makes a BEAM backend a small plugin. Erlang's order-independent
// top-level functions sidestep init cycles for (mutually) recursive definitions.
// Shadow-rule safe: a backend bug is wrong runtime output, never an unsound proof.
type Beam struct{}

func (Beam) Target() string { return "erl" }

// Emit lowers a program to a self-contained escript (run with `escript FILE`).
func (Beam) Emit(p Program) (TargetSource, error) {
	em := &beamEmitter{}
	if p.Nat != nil {
		em.natElim = p.Nat.ElimName
		em.accel = p.Nat.Ops
	}
	var b strings.Builder
	b.WriteString("%% Generated by rune emit (beam backend) — the erased shadow. Do not edit.\n")
	if p.Main != "" {
		mainExpr := beamFn(p.Main) + "()"
		if p.IOMain {
			// An IO main is a deferred world thunk (unit -> A): force by applying unit.
			mainExpr = "ap(" + mainExpr + ", unit)"
		}
		fmt.Fprintf(&b, "main(_) -> io:format(\"~s~n\", [show(%s)]).\n", mainExpr)
	} else {
		b.WriteString("main(_) -> ok.\n")
	}
	b.WriteString(beamRuntime)
	if usesQuot(p) {
		b.WriteString(beamQuotRuntime)
	}
	if usesIO(p) {
		b.WriteString(beamIORuntime)
	}
	// D6 / R-EFFECT: bake the standard OS/IO host bodies when referenced.
	if usesForeign(p, "printNat") {
		b.WriteString("ff_printNat() -> fun(N) -> fun(_U) -> begin io:format(\"~w~n\", [N]), N end end end.\n")
	}
	if usesForeign(p, "getNat") {
		b.WriteString("ff_getNat() -> fun(_U) -> list_to_integer(string:trim(io:get_line(\"\"))) end.\n")
	}
	if usesForeign(p, "timeNanos") {
		b.WriteString("ff_timeNanos() -> fun(_U) -> os:system_time(nanosecond) end.\n")
	}
	if usesForeign(p, "readLineCode") {
		b.WriteString("ff_readLineCode() -> fun(_U) -> L = string:trim(io:get_line(\"\")), {N, P} = lists:foldl(fun(C, {Acc, Pl}) -> {Acc + C * Pl, Pl * 256} end, {0, 1}, L), N + P end.\n")
	}
	// D6 net/fs: the packed-String codec (top-level helpers) + env/file host bodies,
	// over bare Nat codes (the Rune side wraps `bytes`/`codeOf`).
	if usesFileEnv(p) {
		b.WriteString("d6unpack(N) when N =< 1 -> [];\nd6unpack(N) -> [N rem 256 | d6unpack(N div 256)].\n")
		b.WriteString("d6pack(S) -> {N, P} = lists:foldl(fun(C, {Acc, Pl}) -> {Acc + C * Pl, Pl * 256} end, {0, 1}, S), N + P.\n")
	}
	if usesForeign(p, "getEnvCode") {
		b.WriteString("ff_getEnvCode() -> fun(C) -> fun(_U) -> V = case os:getenv(d6unpack(C)) of false -> \"\"; X -> X end, d6pack(V) end end.\n")
	}
	if usesForeign(p, "readFileCode") {
		b.WriteString("ff_readFileCode() -> fun(C) -> fun(_U) -> case file:read_file(d6unpack(C)) of {ok, B} -> d6pack(binary_to_list(B)); _ -> 1 end end end.\n")
	}
	if usesForeign(p, "writeFileCode") {
		b.WriteString("ff_writeFileCode() -> fun(P) -> fun(C) -> fun(_U) -> file:write_file(d6unpack(P), list_to_binary(d6unpack(C))), C end end end.\n")
	}
	if usesForeign(p, "printStrCode") {
		b.WriteString("ff_printStrCode() -> fun(C) -> fun(_U) -> io:format(\"~s~n\", [d6unpack(C)]), C end end.\n")
	}
	// D6 argv + process. escript exposes the script's plain arguments via
	// init:get_plain_arguments/0; halt/1 terminates with an exit status.
	// init:get_plain_arguments/0 under escript is [ScriptName | UserArgs]; drop the
	// head so argv matches the other backends' user-args-only convention.
	if usesForeign(p, "argCountCode") {
		b.WriteString("ff_argCountCode() -> fun(_U) -> max(0, length(init:get_plain_arguments()) - 1) end.\n")
	}
	if usesForeign(p, "argAtCode") {
		b.WriteString("ff_argAtCode() -> fun(I) -> fun(_U) -> A = case init:get_plain_arguments() of [_ | T] -> T; [] -> [] end, case I + 1 =< length(A) of true -> d6pack(lists:nth(I + 1, A)); false -> 1 end end end.\n")
	}
	if usesForeign(p, "exitWith") {
		b.WriteString("ff_exitWith() -> fun(N) -> fun(_U) -> halt(N) end end.\n")
	}
	// D3 machine floats (f64) + the BLAS dot kernel — native Erlang float arithmetic.
	// `Float` is a foreign type surviving erasure as ok/err's type arg (runtime-irrelevant).
	if usesForeign(p, "Float") {
		b.WriteString("ff_Float() -> unit.\n")
	}
	if usesForeign(p, "fromNat") {
		b.WriteString("ff_fromNat() -> fun(N) -> float(N) end.\n")
	}
	if usesForeign(p, "fadd") {
		b.WriteString("ff_fadd() -> fun(A) -> fun(B) -> A + B end end.\n")
	}
	if usesForeign(p, "fsub") {
		b.WriteString("ff_fsub() -> fun(A) -> fun(B) -> A - B end end.\n")
	}
	if usesForeign(p, "fmul") {
		b.WriteString("ff_fmul() -> fun(A) -> fun(B) -> A * B end end.\n")
	}
	if usesForeign(p, "fdiv") {
		b.WriteString("ff_fdiv() -> fun(A) -> fun(B) -> A / B end end.\n")
	}
	if usesForeign(p, "fabsP") {
		b.WriteString("ff_fabsP() -> fun(X) -> abs(X) end.\n")
	}
	if usesForeign(p, "floatToNat") {
		b.WriteString("ff_floatToNat() -> fun(X) -> trunc(X) end.\n")
	}
	if usesForeign(p, "fleqN") {
		b.WriteString("ff_fleqN() -> fun(A) -> fun(B) -> case A =< B of true -> 1; false -> 0 end end end.\n")
	}
	if usesForeign(p, "dot2") {
		b.WriteString("ff_dot2() -> fun(A0) -> fun(A1) -> fun(B0) -> fun(B1) -> A0 * B0 + A1 * B1 end end end end.\n")
	}
	// Arbitrary-length dot: walk two Rune FLists ({c, 1, _, [Head, Tail]}) in lockstep
	// summing products via a named recursive fun — the portable reference (native
	// backends route this through cblas_ddot).
	if usesForeign(p, "dotList") {
		b.WriteString("ff_dotList() -> fun(Xs) -> fun(Ys) -> (fun D(A, B, Acc) -> case {A, B} of {{c, 1, _, [X, Xr]}, {c, 1, _, [Y, Yr]}} -> D(Xr, Yr, Acc + X * Y); _ -> Acc end end)(Xs, Ys, 0.0) end end.\n")
	}
	// D4 interop: npDot is the uniform dot CAPABILITY (NumPy on py, OpenBLAS on C/LLVM);
	// on the other source backends it is the portable reference floor.
	if usesForeign(p, "npDot") {
		b.WriteString("ff_npDot() -> fun(Xs) -> fun(Ys) -> (fun D(A, B, Acc) -> case {A, B} of {{c, 1, _, [X, Xr]}, {c, 1, _, [Y, Yr]}} -> D(Xr, Yr, Acc + X * Y); _ -> Acc end end)(Xs, Ys, 0.0) end end.\n")
	}
	// D4 interop: npMean — the reference floor (hand sum/count); py serves real numpy.mean.
	if usesForeign(p, "npMean") {
		b.WriteString("ff_npMean() -> fun(Xs) -> (fun M(L, S, N) -> case L of {c, 1, _, [X, Xr]} -> M(Xr, S + X, N + 1); _ -> case N of 0 -> 0.0; _ -> S / N end end end)(Xs, 0.0, 0) end.\n")
	}
	// Matrix product sum: flat row-major A (m×k), B (k×n); sum all entries of A·B — the
	// portable triple-loop reference (native backends use cblas_dgemm). Col flattens an
	// FList to an Erlang (1-indexed) list; the comprehension sums A[i*K+P]·B[P*N+J].
	if usesForeign(p, "gemmSum") {
		b.WriteString("ff_gemmSum() -> fun(M) -> fun(K) -> fun(N) -> fun(A) -> fun(B) -> Col = fun C(L) -> case L of {c, 1, _, [X, Xr]} -> [X | C(Xr)]; _ -> [] end end, AL = Col(A), BL = Col(B), lists:sum([ lists:nth(I * K + P + 1, AL) * lists:nth(P * N + J + 1, BL) || I <- lists:seq(0, M - 1), J <- lists:seq(0, N - 1), P <- lists:seq(0, K - 1) ]) end end end end end.\n")
	}
	// D4 interop: npVar — the 2-pass reference floor (mean, then mean of sq deviations).
	if usesForeign(p, "npVar") {
		b.WriteString("ff_npVar() -> fun(Xs) -> Col = fun C(L) -> case L of {c, 1, _, [X, Xr]} -> [X | C(Xr)]; _ -> [] end end, AL = Col(Xs), N = length(AL), case N of 0 -> 0.0; _ -> M = lists:sum(AL) / N, lists:sum([ (X - M) * (X - M) || X <- AL ]) / N end end.\n")
	}
	// D4 interop: npMatSum — the triple-loop reference floor; py serves real numpy matmul.
	if usesForeign(p, "npMatSum") {
		b.WriteString("ff_npMatSum() -> fun(M) -> fun(K) -> fun(N) -> fun(A) -> fun(B) -> Col = fun C(L) -> case L of {c, 1, _, [X, Xr]} -> [X | C(Xr)]; _ -> [] end end, AL = Col(A), BL = Col(B), lists:sum([ lists:nth(I * K + P + 1, AL) * lists:nth(P * N + J + 1, BL) || I <- lists:seq(0, M - 1), J <- lists:seq(0, N - 1), P <- lists:seq(0, K - 1) ]) end end end end end.\n")
	}
	if usesOTP(p) {
		b.WriteString(beamOTPRuntime)
	}
	for _, d := range p.Datas {
		if p.Nat != nil && d.ElimName == p.Nat.ElimName {
			emitNatBeam(&b, *p.Nat)
			continue
		}
		for _, c := range d.Ctors {
			emitCtorBeam(&b, c)
		}
		fmt.Fprintf(&b, "%s() -> %s.\n", beamFn(d.ElimName), em.expr(LowerElim(d), nil))
	}
	for _, def := range p.Defs {
		fmt.Fprintf(&b, "%s() -> %s.\n", beamFn(def.Name), em.expr(def.Body, nil))
	}
	return TargetSource(b.String()), nil
}

func emitCtorBeam(b *strings.Builder, c CtorSpec) {
	fmt.Fprintf(b, "%s() -> ", beamFn(c.Name))
	for i := 0; i < c.Arity; i++ {
		fmt.Fprintf(b, "fun(A%d) -> ", i)
	}
	args := make([]string, c.Arity)
	for i := range args {
		args[i] = fmt.Sprintf("A%d", i)
	}
	fmt.Fprintf(b, "{c, %d, %q, [%s]}", c.Tag, c.Name, strings.Join(args, ", "))
	b.WriteString(strings.Repeat(" end", c.Arity))
	b.WriteString(".\n")
}

func emitNatBeam(b *strings.Builder, n NatSpec) {
	fmt.Fprintf(b, "%s() -> 0.\n", beamFn(n.Zero))
	fmt.Fprintf(b, "%s() -> fun(X) -> X + 1 end.\n", beamFn(n.Succ))
	fmt.Fprintf(b, "%s() -> fun(_M) -> fun(C0) -> fun(C1) -> fun(X) -> nat_elim(C0, C1, X) end end end end.\n", beamFn(n.ElimName))
}

type beamEmitter struct {
	natElim string
	accel   map[string]core.NatOp
}

func (em *beamEmitter) expr(t Ir, env []string) string {
	switch x := t.(type) {
	case IVar:
		if x.Idx < len(env) {
			return env[x.Idx]
		}
		return fmt.Sprintf("V_v%d", x.Idx)
	case IGlobal:
		return beamFn(x.Name) + "()"
	case IForeign:
		// Host-linked accessor: the host defines a 0-arity `ff_<name>/0` function.
		return beamForeign(x.Name) + "()"
	case IUnit:
		return "unit"
	case ILit:
		// Native host literal marshalled at the FFI boundary (B4): an Erlang integer
		// or a BINARY (strings are binaries here so they never alias an integer list).
		if x.Kind == LitNat {
			// C7 / R-NUM: a compressed numeral deploys as a native Erlang integer —
			// the SAME representation NatSpec gives nat (`0`, `X + 1`), arbitrary
			// precision, O(1) source instead of a succ-chain.
			return x.Nat
		}
		if x.Kind == LitStr {
			return fmt.Sprintf("<<%q/utf8>>", x.Str)
		}
		if x.Kind == LitPtr {
			// Opaque host pointer (B4): a tagged tuple so it never aliases an
			// integer, and show renders it as a fixed marker, not its handle.
			return fmt.Sprintf("{ptr, %d}", x.Int)
		}
		return fmt.Sprintf("%d", x.Int)
	case ILam:
		n := freshBeam(x.Name, env)
		return fmt.Sprintf("fun(%s) -> %s end", n, em.expr(x.Body, prepend(n, env)))
	case IApp:
		if out, ok := em.accelDispatch(x, env); ok {
			return out
		}
		if out, ok := em.natDispatch(x, env); ok {
			return out
		}
		return fmt.Sprintf("ap(%s, %s)", em.expr(x.Fn, env), em.expr(x.Arg, env))
	case ILet:
		n := freshBeam(x.Name, env)
		return fmt.Sprintf("begin %s = %s, %s end", n, em.expr(x.Val, env), em.expr(x.Body, prepend(n, env)))
	case IPair:
		return fmt.Sprintf("{pair, %s, %s}", em.expr(x.A, env), em.expr(x.B, env))
	case IFst:
		return fmt.Sprintf("element(2, %s)", em.expr(x.P, env))
	case ISnd:
		return fmt.Sprintf("element(3, %s)", em.expr(x.P, env))
	case IField:
		return fmt.Sprintf("lists:nth(%d, element(4, %s))", x.Index+1, em.expr(x.Scrut, env))
	case ICase:
		var b strings.Builder
		fmt.Fprintf(&b, "case element(2, %s) of ", em.expr(x.Scrut, env))
		for _, arm := range x.Arms {
			fmt.Fprintf(&b, "%d -> %s; ", arm.Tag, em.expr(arm.Body, env))
		}
		b.WriteString("_ -> erlang:error(impossible) end")
		return b.String()
	default:
		panic("codegen(beam): unknown IR node")
	}
}

// accelDispatch emits a registered accel-op def on two args as native Erlang
// integer arithmetic (C7 / R-NUM Decision 4): add → `(a + b)`, mul → `(a * b)`,
// monus → saturating `max(a - b, 0)` (Peano monus floors at 0). Erlang integers
// are arbitrary precision, the SAME representation NatSpec gives nat.
func (em *beamEmitter) accelDispatch(app IApp, env []string) (string, bool) {
	op, a, b, ok := accelMatch(app, em.accel)
	if !ok {
		return "", false
	}
	ea, eb := em.expr(a, env), em.expr(b, env)
	switch op {
	case core.NatOpAdd:
		return fmt.Sprintf("(%s + %s)", ea, eb), true
	case core.NatOpMul:
		return fmt.Sprintf("(%s * %s)", ea, eb), true
	case core.NatOpMonus:
		return fmt.Sprintf("max((%s) - (%s), 0)", ea, eb), true
	case core.NatOpDiv:
		// A0/B0 are FRESH fun-bound variables (uppercase-initial). NOT `$A`/`$B`:
		// in Erlang `$x` is the character-code literal of x, so `fun($A, $B) -> …`
		// matches only the integers 65/66 — a real divisor like 256 hits no clause
		// (exit 127 at runtime). Saturating to the prelude's a//0 = 0 contract.
		return fmt.Sprintf("(fun(A0, B0) -> case B0 of 0 -> 0; _ -> A0 div B0 end end)(%s, %s)", ea, eb), true
	case core.NatOpMod:
		return fmt.Sprintf("(fun(A0, B0) -> case B0 of 0 -> A0; _ -> A0 rem B0 end end)(%s, %s)", ea, eb), true
	}
	return "", false
}

func (em *beamEmitter) natDispatch(app IApp, env []string) (string, bool) {
	if em.natElim == "" {
		return "", false
	}
	var args []Ir
	t := Ir(app)
	for {
		a, ok := t.(IApp)
		if !ok {
			break
		}
		args = append([]Ir{a.Arg}, args...)
		t = a.Fn
	}
	g, ok := t.(IGlobal)
	if !ok || g.Name != em.natElim || len(args) < 4 {
		return "", false
	}
	c1lam, ok := args[2].(ILam)
	if !ok {
		return "", false
	}
	inner, ok := c1lam.Body.(ILam)
	if !ok || usesVar(inner.Body, 0) {
		return "", false
	}
	out := fmt.Sprintf("nat_d(%s, %s, %s)",
		em.expr(args[1], env), em.expr(args[2], env), em.expr(args[3], env))
	for _, extra := range args[4:] {
		out = fmt.Sprintf("ap(%s, %s)", out, em.expr(extra, env))
	}
	return out, true
}

func freshBeam(hint string, env []string) string {
	n := "V_" + beamMangle(hint)
	if n == "V_" {
		n = "V_x"
	}
	for {
		clash := false
		for _, e := range env {
			if e == n {
				clash = true
				break
			}
		}
		if !clash {
			return n
		}
		n += "_"
	}
}

// beamFn is the 0-arity function name for a definition/constructor/eliminator.
func beamFn(n string) string { return "d_" + beamMangle(n) }

// beamForeign is the 0-arity host-accessor name for a `foreign` axiom.
func beamForeign(n string) string { return "ff_" + beamMangle(n) }

// beamMangle turns a rune identifier into the body of an Erlang atom/variable
// suffix: operators map through jsOpNames (with $ → op_), primes and any other
// non-alphanumeric become _.
func beamMangle(n string) string {
	if m, ok := jsOpNames[n]; ok {
		n = strings.ReplaceAll(m, "$", "op_")
	} else if i := strings.IndexByte(n, '$'); i > 0 {
		if m, ok := jsOpNames[n[:i]]; ok {
			n = strings.ReplaceAll(m, "$", "op_") + n[i:]
		}
	}
	var b strings.Builder
	for _, r := range n {
		switch {
		case r >= 'a' && r <= 'z', r >= 'A' && r <= 'Z', r >= '0' && r <= '9', r == '_':
			b.WriteRune(r)
		default:
			b.WriteByte('_')
		}
	}
	return b.String()
}

// beamRuntime is the shared helpers: application, the nat eliminator/destructor,
// and the structural show (erased unit fields are skipped, matching JS/Go).
const beamRuntime = `ap(F, X) -> F(X).
nat_elim(C0, _C1, 0) -> C0;
nat_elim(C0, C1, N) when N > 0 -> ap(ap(C1, N - 1), nat_elim(C0, C1, N - 1)).
nat_d(C0, _C1, 0) -> C0;
nat_d(_C0, C1, N) when N > 0 -> ap(ap(C1, N - 1), unit).
show(V) ->
  if
    V =:= unit -> "()";
    is_integer(V) -> integer_to_list(V);
    is_binary(V) -> binary_to_list(V);
    is_function(V) -> "<function>";
    true -> show_t(V)
  end.
show_t({ptr, _}) -> "<ptr>";
show_t({pair, A, B}) -> "(" ++ show(A) ++ ", " ++ show(B) ++ ")";
show_t({c, _Tag, Name, Args}) ->
  lists:foldl(fun(A, S) ->
    case A =:= unit of
      true -> S;
      false ->
        R = show(A),
        case lists:member($ , R) of
          true -> S ++ " (" ++ R ++ ")";
          false -> S ++ " " ++ R
        end
    end
  end, Name, Args).
`

// beamQuotRuntime is the erased quotient builtin group (v2): the carrier with the
// proofs as units.
const beamQuotRuntime = `d_qin() -> fun(_A) -> fun(_R) -> fun(X) -> X end end end.
d_qsound() -> fun(_A) -> fun(_R) -> fun(_X) -> fun(_Y) -> fun(_P) -> unit end end end end end.
d_qlift() -> fun(_A) -> fun(_R) -> fun(_B) -> fun(F) -> fun(_Resp) -> fun(Q) -> ap(F, Q) end end end end end end.
d_qind() -> fun(_A) -> fun(_R) -> fun(_P) -> fun(_H) -> fun(_Q) -> unit end end end end end.
`

// beamIORuntime is the erased IO monad (R-EFFECT / C3): IO A is a unit-argument
// thunk, so `ap` forces it by applying unit.
const beamIORuntime = `d_pureIO() -> fun(_A) -> fun(A) -> fun(_U) -> A end end end.
d_bindIO() -> fun(_A) -> fun(_B) -> fun(M) -> fun(K) -> fun(_U) -> ap(ap(K, ap(M, unit)), unit) end end end end end.
`

// otpPrims are the live-runtime OTP primitives (D5 / R-OTP, Layer R0). A program
// referencing any of them as a `foreign` axiom is given the real BEAM actor
// runtime (beamOTPRuntime) — genuine processes, not a functional model. The names
// are the contract: a listing must name its foreign axioms exactly these for the
// host bindings below to apply.
var otpPrims = map[string]bool{
	"Pid":         true,
	"primSpawn":   true,
	"primSelf":    true,
	"primSend":    true,
	"primReceive": true,
	"primMonitor": true,
	"primExit":    true,
}

// usesOTP reports whether the program references an OTP runtime primitive (a
// `foreign` axiom erased to an IForeign accessor whose name is one of otpPrims).
func usesOTP(p Program) bool {
	var walk func(Ir) bool
	walk = func(t Ir) bool {
		switch x := t.(type) {
		case IForeign:
			return otpPrims[x.Name]
		case ILam:
			return walk(x.Body)
		case IApp:
			return walk(x.Fn) || walk(x.Arg)
		case ILet:
			return walk(x.Val) || walk(x.Body)
		case IPair:
			return walk(x.A) || walk(x.B)
		case IFst:
			return walk(x.P)
		case ISnd:
			return walk(x.P)
		default:
			return false
		}
	}
	for _, d := range p.Defs {
		if walk(d.Body) {
			return true
		}
	}
	return false
}

// beamOTPRuntime is the LIVE OTP runtime (D5 / R-OTP Layer R0) — the host bindings
// for the `foreign` process primitives, mapping each onto a real BEAM construct
// (the "near-free on BEAM" gift: spawn/!/receive/self ARE the Erlang runtime). It
// is the host-accessor body the IForeign mechanism calls (ff_<name>/0), exactly as
// the FFI conformance harness injects ff_hostId, but shipped WITH the compiler so an
// OTP program deploys and runs unaided.
//
// Each op respects the IO-thunk erasure (IO A = `fun(_U) -> A`): the leading type
// argument (M : U) erases to a unit slot (_M), and the final unit slot is the
// world the runtime forces. A typed `Pid M` is the message it accepts (Gleam's
// Subject), erased to a bare BEAM pid.
//
//	primSelf    M        ~> self()
//	primSpawn   M body   ~> spawn a process running `body self()`; return its pid
//	primSend    M p x    ~> p ! x  (async, fire-and-forget; FIFO per sender)
//	primReceive M        ~> receive the next message (FIFO)
//
// The FAULT layer (D5 / R-OTP Layer R2 — the live half of the ch206 fault LTS):
//
//	primExit    M p      ~> exit(p, crashed)  — inject a CRASH: terminate process p
//	                        with reason `crashed` (untrapped ⇒ the process dies).
//	primMonitor M p      ~> erlang:monitor(process, p), then block until p dies and
//	                        consume the DOWN — the DETECT rule made live. Robust to
//	                        the crash-before-monitor race: monitoring an already-dead
//	                        pid delivers an immediate DOWN (reason `noproc`), so the
//	                        supervisor always observes the failure exactly once.
const beamOTPRuntime = `ff_Pid() -> fun(_M) -> unit end.
ff_primSelf() -> fun(_M) -> fun(_U) -> self() end end.
ff_primSpawn() -> fun(_M) -> fun(B) -> fun(_U) -> spawn(fun() -> ap(ap(B, self()), unit) end) end end end.
ff_primSend() -> fun(_M) -> fun(P) -> fun(X) -> fun(_U) -> begin P ! X, unit end end end end end.
ff_primReceive() -> fun(_M) -> fun(_U) -> receive Msg -> Msg end end end.
ff_primExit() -> fun(_M) -> fun(P) -> fun(_U) -> begin exit(P, crashed), unit end end end end.
ff_primMonitor() -> fun(_M) -> fun(P) -> fun(_U) -> begin erlang:monitor(process, P), receive {'DOWN', _Ref, process, _Pid, _Reason} -> unit end end end end end.
`
