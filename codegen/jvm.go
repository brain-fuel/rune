// jvmRuntime is the sealed value interface and its record variants (Java 25).
const jvmRuntime = `import java.util.function.Function;
import java.util.concurrent.*;
sealed interface V permits VUnit, VInt, VNat, VFloat, VStr, VPtr, VFun, VCtor, VPair, VProc {}
record VUnit() implements V {}
record VInt(long n) implements V {}            // host machine int (FFI LitInt)
record VNat(java.math.BigInteger n) implements V {} // builtin-nat: arbitrary precision
record VFloat(double d) implements V {}        // D3 machine float (IEEE-754 f64)
record VStr(String s) implements V {}
record VPtr(long h) implements V {} // opaque host pointer (B4): never shown structurally
record VFun(Function<V,V> f) implements V {}
record VCtor(int tag, String name, V[] args) implements V {}
record VPair(V a, V b) implements V {}
record VProc(BlockingQueue<V> mb, CountDownLatch down) implements V {} // D5 OTP: a live process (typed mailbox + DOWN)
`

// jvmHelpers are the shared runtime helpers (methods of class `main`): application,
// machine-int access, projections, the nat eliminator/destructor, the impossible
// head, and the structural show (erased unit fields skipped, matching JS/Go/Rust).
const jvmHelpers = `  static final V UNIT = new VUnit();
  static V unit() { return UNIT; }
  static V fun(Function<V,V> f) { return new VFun(f); }
  static V ap(V f, V x) {
    return switch (f) {
      case VFun g -> g.f().apply(x);
      default -> throw new RuntimeException("rune: applied a non-function");
    };
  }
  static long _int(V v) {
    return switch (v) { case VInt i -> i.n(); default -> throw new RuntimeException("rune: expected an int"); };
  }
  static java.math.BigInteger _nat(V v) {
    return switch (v) { case VNat i -> i.n(); default -> throw new RuntimeException("rune: expected a nat"); };
  }
  static double _float(V v) {
    return switch (v) { case VFloat f -> f.d(); default -> throw new RuntimeException("rune: expected a float"); };
  }
  static V _fst(V p) {
    return switch (p) { case VPair pr -> pr.a(); default -> throw new RuntimeException("rune: fst of a non-pair"); };
  }
  static V _snd(V p) {
    return switch (p) { case VPair pr -> pr.b(); default -> throw new RuntimeException("rune: snd of a non-pair"); };
  }
  static int _tag_of(V v) {
    return switch (v) { case VCtor c -> c.tag(); default -> throw new RuntimeException("rune: scrutinee is not a constructor"); };
  }
  static V _field(V v, int i) {
    return switch (v) { case VCtor c -> c.args()[i]; default -> throw new RuntimeException("rune: field of a non-constructor"); };
  }
  static V _impossible() { throw new RuntimeException("impossible: unmatched constructor tag"); }
  static V _nat_elim(V c0, V c1, V x) {
    V acc = c0;
    java.math.BigInteger n = _nat(x);
    for (java.math.BigInteger k = java.math.BigInteger.ZERO; k.compareTo(n) < 0; k = k.add(java.math.BigInteger.ONE)) {
      acc = ap(ap(c1, new VNat(k)), acc);
    }
    return acc;
  }
  static V _nat_d(V c0, V c1, V x) {
    java.math.BigInteger n = _nat(x);
    return n.signum() == 0 ? c0 : ap(ap(c1, new VNat(n.subtract(java.math.BigInteger.ONE))), unit());
  }
  static V _nat_add(V a, V b) { return new VNat(_nat(a).add(_nat(b))); }
  static V _nat_mul(V a, V b) { return new VNat(_nat(a).multiply(_nat(b))); }
  static V _nat_monus(V a, V b) {
    java.math.BigInteger x = _nat(a), y = _nat(b);
    return new VNat(x.compareTo(y) > 0 ? x.subtract(y) : java.math.BigInteger.ZERO);
  }
  static String _show(V v) {
    return switch (v) {
      case VUnit u -> "()";
      case VInt i -> Long.toString(i.n());
      case VNat i -> i.n().toString();
      case VFloat f -> Double.toString(f.d());
      case VProc p -> "<proc>";
      case VStr s -> s.s();
      case VPtr p -> "<ptr>";
      case VFun f -> "<function>";
      case VPair p -> "(" + _show(p.a()) + ", " + _show(p.b()) + ")";
      case VCtor c -> {
        StringBuilder sb = new StringBuilder(c.name());
        for (V a : c.args()) {
          if (a instanceof VUnit) continue;
          String r = _show(a);
          if (r.contains(" ")) sb.append(" (").append(r).append(")");
          else sb.append(" ").append(r);
        }
        yield sb.toString();
      }
    };
  }
`

// jvmIORuntime is the erased IO monad (R-EFFECT / C3): IO A is a deferred thunk
// encoded as a unit-argument closure, forced by applying unit; pureIO returns a
// constant thunk, bindIO sequences. Type arguments arrive as units.
func jvmIORuntime() string {
	return "  static V pureIO_d() { return " + jvmCurry([]string{"_a", "a", "_u"}, "a") + "; }\n" +
		"  static V bindIO_d() { return " + jvmCurry([]string{"_a", "_b", "m", "k", "_u"},
		"ap(ap(k, ap(m, unit())), unit())") + "; }\n"
}

// jvmQuotRuntime is the erased quotient builtin group (v2): a quotient compiles to
// its carrier — qin is the identity on its point, qlift applies the lifted function,
// and the proof-valued members (qsound, qind) are unit-producers.
var jvmQuotRuntime = "  static V qin_d() { return " + jvmCurry([]string{"_a", "_r", "x"}, "x") + "; }\n" +
	"  static V qsound_d() { return " + jvmCurry([]string{"_a", "_r", "_x", "_y", "_p"}, "unit()") + "; }\n" +
	"  static V qlift_d() { return " + jvmCurry([]string{"_a", "_r", "_b", "f", "_resp", "q"}, "ap(f, q)") + "; }\n" +
	"  static V qind_d() { return " + jvmCurry([]string{"_a", "_r", "_p", "_h", "_q"}, "unit()") + "; }\n"
