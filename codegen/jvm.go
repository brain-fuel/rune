package codegen

import (
	"fmt"
	"strings"

	"goforge.dev/rune/v3/core"
)

// JVM is a source-emitter backend (B-track / multi-backend) targeting JVM 25+:
// idiomatic modern Java over the SAME erased IR as JS/Python/Go/Rust/BEAM. A runtime
// value is a SEALED interface `V` with RECORD variants (unit / machine int / curried
// closure / tagged constructor / pair); application, projection, show, and the nat
// eliminator dispatch by PATTERN-MATCHING SWITCH over those records (Java 21+, final
// in 25); the entrypoint runs the show on a VIRTUAL THREAD (Java 21+, GA), whose
// growable stack absorbs the deep erased recursion the Rust/Python backends spawn a
// 512 MB OS-thread for. Each top-level definition is a static thunk method
// `name_d()`; references call it, so (mutual) recursion and eliminators are ordinary
// method calls — no cyclic-static problem. The single public class is `main`
// (so the file is `main.java`); javac/java come from a JDK 25 toolchain.
// Shadow-rule safe: a backend bug is wrong runtime output, never an unsound proof.
type JVM struct{}

func (JVM) Target() string { return "jvm" }

// Emit lowers a program to a self-contained Java 25 source file (javac-compilable).
func (JVM) Emit(p Program) (TargetSource, error) {
	em := &jvmEmitter{}
	if p.Nat != nil {
		em.natElim = p.Nat.ElimName
		em.accel = p.Nat.Ops
	}
	var b strings.Builder
	b.WriteString("// Generated by rune emit (jvm backend, Java 25+) — the erased shadow. Do not edit.\n")
	b.WriteString(jvmRuntime)
	b.WriteString("final class main {\n")
	b.WriteString(jvmHelpers)
	if usesQuot(p) {
		b.WriteString(jvmQuotRuntime)
	}
	if usesIO(p) {
		b.WriteString(jvmIORuntime())
	}
	// D6/D3: standard host bodies baked as `static V <name>()` methods (the foreign
	// accessor mechanism), gated by reference — the JVM counterparts of the js/py/go/
	// rust host bodies, so the f64 element type + a printing IO program run on Java 25.
	if usesForeign(p, "printNat") {
		b.WriteString("  static V printNat() { return fun(n -> fun(u -> { System.out.println(_nat(n)); return n; })); }\n")
	}
	if usesForeign(p, "getNat") || usesForeign(p, "readLineCode") {
		b.WriteString("  static java.io.BufferedReader __stdin = new java.io.BufferedReader(new java.io.InputStreamReader(System.in));\n")
	}
	if usesForeign(p, "getNat") {
		b.WriteString("  static V getNat() { return fun(u -> { try { return new VNat(new java.math.BigInteger(__stdin.readLine().trim())); } catch (Exception e) { return new VNat(java.math.BigInteger.ZERO); } }); }\n")
	}
	// readLineCode: read one stdin line, return its PACKED bytes (B4: first byte
	// least-significant, 0x01 sentinel on top) — the same inline encode as the go/js
	// bodies (no __h2s dependency, so a read-only program needs no codec block).
	// EOF/error surfaces as the EMPTY string (code 1), the D6 failure convention.
	if usesForeign(p, "readLineCode") {
		b.WriteString("  static V readLineCode() { return fun(u -> { try { String s = __stdin.readLine(); if (s == null) s = \"\"; var n = java.math.BigInteger.ONE; var m = java.math.BigInteger.valueOf(256); for (int i = s.length() - 1; i >= 0; i--) { n = n.multiply(m).add(java.math.BigInteger.valueOf(s.charAt(i))); } return new VNat(n); } catch (Exception e) { return new VNat(java.math.BigInteger.ONE); } }); }\n")
	}
	// Phase 0 — real byte strings (Bin): the value domain gained a VBytes(int[])
	// record (in jvmRuntime); these are its host ops. Nats are VNat BigIntegers.
	if usesBin(p) {
		b.WriteString("  static V binEmpty() { return new VBytes(new int[0]); }\n")
		b.WriteString("  static V binCons() { return fun(c -> fun(b -> { int[] o = ((VBytes) b).b(); int[] nv = new int[o.length + 1]; nv[0] = _nat(c).intValue() & 255; System.arraycopy(o, 0, nv, 1, o.length); return new VBytes(nv); })); }\n")
		b.WriteString("  static V binLen() { return fun(b -> new VNat(java.math.BigInteger.valueOf(((VBytes) b).b().length))); }\n")
		b.WriteString("  static V binAt() { return fun(b -> fun(i -> { int[] bs = ((VBytes) b).b(); int k = _nat(i).intValue(); return new VNat(java.math.BigInteger.valueOf((k >= 0 && k < bs.length) ? bs[k] : 0)); })); }\n")
		b.WriteString("  static V printBin() { return fun(b -> fun(u -> { System.out.println(_binShow(((VBytes) b).b())); return UNIT; })); }\n")
	}
	// Phase 1 — uniform socket FFI: V cannot box a Socket, so handles are long tokens
	// (VNat) into a static table; payloads are Bin (VBytes). Blocking (virtual thread).
	if usesNet(p) {
		b.WriteString("  static java.util.concurrent.ConcurrentHashMap<Long, java.net.Socket> __socks = new java.util.concurrent.ConcurrentHashMap<>();\n")
		b.WriteString("  static long __sockId = 0;\n")
		b.WriteString("  static byte[] _binbytes(V v) { int[] a = ((VBytes) v).b(); byte[] o = new byte[a.length]; for (int i = 0; i < a.length; i++) o[i] = (byte) a[i]; return o; }\n")
		b.WriteString("  static V sockConnect() { return fun(host -> fun(port -> fun(u -> { try { var h = new String(_binbytes(host), java.nio.charset.StandardCharsets.ISO_8859_1); var s = new java.net.Socket(h, _nat(port).intValue()); long id = ++__sockId; __socks.put(id, s); return new VNat(java.math.BigInteger.valueOf(id)); } catch (Exception e) { return new VNat(java.math.BigInteger.ZERO); } }))); }\n")
		b.WriteString("  static V sockWrite() { return fun(c -> fun(data -> fun(u -> { try { var s = __socks.get(_nat(c).longValue()); var b = _binbytes(data); var o = s.getOutputStream(); o.write(b); o.flush(); return new VNat(java.math.BigInteger.valueOf(b.length)); } catch (Exception e) { return new VNat(java.math.BigInteger.ZERO); } }))); }\n")
		b.WriteString("  static V sockRead() { return fun(c -> fun(n -> fun(u -> { try { var s = __socks.get(_nat(c).longValue()); int k = _nat(n).intValue(); byte[] buf = new byte[k]; int m = s.getInputStream().read(buf); if (m < 0) m = 0; int[] out = new int[m]; for (int i = 0; i < m; i++) out[i] = buf[i] & 255; return new VBytes(out); } catch (Exception e) { return new VBytes(new int[0]); } }))); }\n")
		b.WriteString("  static java.util.concurrent.ConcurrentHashMap<Long, java.net.ServerSocket> __servers = new java.util.concurrent.ConcurrentHashMap<>();\n")
		b.WriteString("  static V sockClose() { return fun(c -> fun(u -> { try { long id = _nat(c).longValue(); var s = __socks.remove(id); if (s != null) s.close(); var srv = __servers.remove(id); if (srv != null) srv.close(); } catch (Exception e) {} return UNIT; })); }\n")
		b.WriteString("  static V sockListen() { return fun(port -> fun(u -> { try { var srv = new java.net.ServerSocket(); srv.setReuseAddress(true); srv.bind(new java.net.InetSocketAddress(\"127.0.0.1\", _nat(port).intValue())); long id = ++__sockId; __servers.put(id, srv); return new VNat(java.math.BigInteger.valueOf(id)); } catch (Exception e) { return new VNat(java.math.BigInteger.ZERO); } })); }\n")
		b.WriteString("  static V sockAccept() { return fun(c -> fun(u -> { try { var srv = __servers.get(_nat(c).longValue()); var s = srv.accept(); long id = ++__sockId; __socks.put(id, s); return new VNat(java.math.BigInteger.valueOf(id)); } catch (Exception e) { return new VNat(java.math.BigInteger.ZERO); } })); }\n")
	}
	// Phase 1 — expanded OS/filesystem layer (Bin = VBytes via java.nio.file).
	if usesFS(p) {
		b.WriteString("  static byte[] _fsbytes(V v) { int[] a = ((VBytes) v).b(); byte[] o = new byte[a.length]; for (int i = 0; i < a.length; i++) o[i] = (byte) a[i]; return o; }\n")
		b.WriteString("  static String _fspath(V v) { return new String(_fsbytes(v), java.nio.charset.StandardCharsets.ISO_8859_1); }\n")
		b.WriteString("  static V fsWrite() { return fun(path -> fun(content -> fun(u -> { try { byte[] b = _fsbytes(content); java.nio.file.Files.write(java.nio.file.Path.of(_fspath(path)), b); return new VNat(java.math.BigInteger.valueOf(b.length)); } catch (Exception e) { return new VNat(java.math.BigInteger.ZERO); } }))); }\n")
		b.WriteString("  static V fsRead() { return fun(path -> fun(u -> { try { byte[] b = java.nio.file.Files.readAllBytes(java.nio.file.Path.of(_fspath(path))); int[] o = new int[b.length]; for (int i = 0; i < b.length; i++) o[i] = b[i] & 255; return new VBytes(o); } catch (Exception e) { return new VBytes(new int[0]); } })); }\n")
		b.WriteString("  static V fsExists() { return fun(path -> fun(u -> new VNat(java.nio.file.Files.exists(java.nio.file.Path.of(_fspath(path))) ? java.math.BigInteger.ONE : java.math.BigInteger.ZERO))); }\n")
		b.WriteString("  static V fsRemove() { return fun(path -> fun(u -> { try { return new VNat(java.nio.file.Files.deleteIfExists(java.nio.file.Path.of(_fspath(path))) ? java.math.BigInteger.ONE : java.math.BigInteger.ZERO); } catch (Exception e) { return new VNat(java.math.BigInteger.ZERO); } })); }\n")
		b.WriteString("  static V fsMkdir() { return fun(path -> fun(u -> { try { java.nio.file.Files.createDirectories(java.nio.file.Path.of(_fspath(path))); return new VNat(java.math.BigInteger.ONE); } catch (Exception e) { return new VNat(java.math.BigInteger.ZERO); } })); }\n")
	}
	// Phase 1 — os/exec: run program with one arg, capture stdout.
	if usesProc(p) {
		b.WriteString("  static String _procstr(V v) { int[] a = ((VBytes) v).b(); byte[] o = new byte[a.length]; for (int i = 0; i < a.length; i++) o[i] = (byte) a[i]; return new String(o, java.nio.charset.StandardCharsets.ISO_8859_1); }\n")
		b.WriteString("  static V procRun() { return fun(prog -> fun(arg -> fun(u -> { try { var p = new ProcessBuilder(_procstr(prog), _procstr(arg)).start(); byte[] b = p.getInputStream().readAllBytes(); p.waitFor(); int[] o = new int[b.length]; for (int i = 0; i < b.length; i++) o[i] = b[i] & 255; return new VBytes(o); } catch (Exception e) { return new VBytes(new int[0]); } }))); }\n")
	}
	// Phase 3 — crypto: host sha256 (pure).
	if usesCrypto(p) {
		b.WriteString("  static byte[] _cryptobytes(V v) { int[] a = ((VBytes) v).b(); byte[] o = new byte[a.length]; for (int i = 0; i < a.length; i++) o[i] = (byte) a[i]; return o; }\n")
		b.WriteString("  static V sha256() { return fun(data -> { try { byte[] h = java.security.MessageDigest.getInstance(\"SHA-256\").digest(_cryptobytes(data)); int[] o = new int[h.length]; for (int i = 0; i < h.length; i++) o[i] = h[i] & 255; return new VBytes(o); } catch (Exception e) { return new VBytes(new int[0]); } }); }\n")
	}
	// Phase 3 — TLS: HTTPS GET via HttpsURLConnection with a trust-all context
	// (self-signed). Self-contained (no _binbytes dependency on usesNet).
	if usesTLS(p) {
		b.WriteString("  static String _tlsstr(V v) { int[] a = ((VBytes) v).b(); byte[] o = new byte[a.length]; for (int i = 0; i < a.length; i++) o[i] = (byte) a[i]; return new String(o, java.nio.charset.StandardCharsets.ISO_8859_1); }\n")
		b.WriteString("  static V tlsGet() { return fun(host -> fun(port -> fun(path -> fun(u -> { try { var h = _tlsstr(host); var pa = _tlsstr(path); var tm = new javax.net.ssl.TrustManager[]{ new javax.net.ssl.X509TrustManager() { public void checkClientTrusted(java.security.cert.X509Certificate[] c, String s) {} public void checkServerTrusted(java.security.cert.X509Certificate[] c, String s) {} public java.security.cert.X509Certificate[] getAcceptedIssuers() { return new java.security.cert.X509Certificate[0]; } } }; var sc = javax.net.ssl.SSLContext.getInstance(\"TLS\"); sc.init(null, tm, new java.security.SecureRandom()); var url = new java.net.URL(\"https://\" + h + \":\" + _nat(port).intValue() + pa); var c = (javax.net.ssl.HttpsURLConnection) url.openConnection(); c.setSSLSocketFactory(sc.getSocketFactory()); c.setHostnameVerifier((hn, se) -> true); byte[] bb = c.getInputStream().readAllBytes(); int[] o = new int[bb.length]; for (int i = 0; i < bb.length; i++) o[i] = bb[i] & 255; return new VBytes(o); } catch (Exception e) { return new VBytes(new int[0]); } })))); }\n")
	}
	if usesForeign(p, "Float") {
		b.WriteString("  static V Float() { return UNIT; }\n") // foreign type, erased
	}
	if usesForeign(p, "fromNat") {
		b.WriteString("  static V fromNat() { return fun(n -> new VFloat(_nat(n).doubleValue())); }\n")
	}
	if usesForeign(p, "fadd") {
		b.WriteString("  static V fadd() { return fun(a -> fun(b -> new VFloat(_float(a) + _float(b)))); }\n")
	}
	if usesForeign(p, "fsub") {
		b.WriteString("  static V fsub() { return fun(a -> fun(b -> new VFloat(_float(a) - _float(b)))); }\n")
	}
	if usesForeign(p, "fmul") {
		b.WriteString("  static V fmul() { return fun(a -> fun(b -> new VFloat(_float(a) * _float(b)))); }\n")
	}
	if usesForeign(p, "fdiv") {
		b.WriteString("  static V fdiv() { return fun(a -> fun(b -> new VFloat(_float(a) / _float(b)))); }\n")
	}
	if usesForeign(p, "fabsP") {
		b.WriteString("  static V fabsP() { return fun(x -> new VFloat(Math.abs(_float(x)))); }\n")
	}
	if usesForeign(p, "fsqrt") {
		b.WriteString("  static V fsqrt() { return fun(x -> new VFloat(Math.sqrt(_float(x)))); }\n")
	}
	if usesForeign(p, "fpow") {
		b.WriteString("  static V fpow() { return fun(a -> fun(e -> new VFloat(Math.pow(_float(a), _float(e))))); }\n")
	}
	if usesForeign(p, "floatToNat") {
		b.WriteString("  static V floatToNat() { return fun(x -> new VNat(java.math.BigInteger.valueOf((long) _float(x)))); }\n")
	}
	if usesForeign(p, "fleqN") {
		b.WriteString("  static V fleqN() { return fun(a -> fun(b -> new VNat(_float(a) <= _float(b) ? java.math.BigInteger.ONE : java.math.BigInteger.ZERO))); }\n")
	}
	if usesForeign(p, "dot2") {
		b.WriteString("  static V dot2() { return fun(a0 -> fun(a1 -> fun(b0 -> fun(b1 -> new VFloat(_float(a0)*_float(b0) + _float(a1)*_float(b1)))))); }\n")
	}
	// D5 / R-OTP: the JVM scheduler shim. Java 25 virtual threads CAN block on a queue,
	// so the actor blocks on a real receive — no CPS (the JS shim's problem). A Pid is a
	// VProc (a BlockingQueue mailbox + a CountDownLatch DOWN signal); goroutine-local
	// identity is a ThreadLocal. Mirrors goOTPRuntime/beamOTPRuntime on the JVM.
	if usesOTP(p) {
		b.WriteString("  static final ThreadLocal<VProc> __self = new ThreadLocal<>();\n")
		b.WriteString("  static VProc __me() { VProc p = __self.get(); if (p == null) { p = new VProc(new LinkedBlockingQueue<V>(), new CountDownLatch(1)); __self.set(p); } return p; }\n")
		b.WriteString("  static V Pid() { return fun(_M -> UNIT); }\n")
		b.WriteString("  static V primSelf() { return fun(_M -> fun(_u -> __me())); }\n")
		b.WriteString("  static V primSpawn() { return fun(_M -> fun(b -> fun(_u -> { VProc pr = new VProc(new LinkedBlockingQueue<V>(), new CountDownLatch(1)); Thread.ofVirtual().start(() -> { __self.set(pr); try { ap(ap(b, pr), unit()); } finally { pr.down().countDown(); } }); return pr; }))); }\n")
		b.WriteString("  static V primSend() { return fun(_M -> fun(p -> fun(x -> fun(_u -> { try { ((VProc)p).mb().put(x); } catch (InterruptedException e) { Thread.currentThread().interrupt(); } return UNIT; })))); }\n")
		b.WriteString("  static V primReceive() { return fun(_M -> fun(_u -> { try { return __me().mb().take(); } catch (InterruptedException e) { throw new RuntimeException(e); } })); }\n")
		b.WriteString("  static V primExit() { return fun(_M -> fun(p -> fun(_u -> { ((VProc)p).down().countDown(); return UNIT; }))); }\n")
		b.WriteString("  static V primMonitor() { return fun(_M -> fun(p -> fun(_u -> { try { ((VProc)p).down().await(); } catch (InterruptedException e) { Thread.currentThread().interrupt(); } return UNIT; }))); }\n")
	}
	if usesForeign(p, "printStrCode") {
		b.WriteString("  static V printStrCode() { return fun(c -> fun(_u -> { System.out.println(__s2h(c)); return c; })); }\n")
	}
	if usesLiveKV(p) || usesFileEnv(p) || usesStream(p) {
		// E4: the JVM live-broker data-plane binding. java.net.Socket BLOCKS, so RESP
		// is synchronous (like Go, unlike the JS async path). A packed String code is a
		// VNat bignum; __s2h/__h2s are the ASCII byte codec (first byte LSB + 0x01).
		b.WriteString("  static String __s2h(V v) { var b = _nat(v); var m = java.math.BigInteger.valueOf(256); var sb = new StringBuilder(); while (b.compareTo(java.math.BigInteger.ONE) > 0) { var qr = b.divideAndRemainder(m); sb.append((char) qr[1].intValue()); b = qr[0]; } return sb.toString(); }\n")
		b.WriteString("  static V __h2s(String s) { var n = java.math.BigInteger.ONE; var m = java.math.BigInteger.valueOf(256); for (int i = s.length() - 1; i >= 0; i--) { n = n.multiply(m).add(java.math.BigInteger.valueOf(s.charAt(i))); } return new VNat(n); }\n")
		b.WriteString("  static String __kvAddr() { var u = System.getenv(\"WAVELET_KV_URL\"); if (u == null) u = \"\"; u = u.replace(\"redis://\", \"\"); if (u.isEmpty()) u = \"localhost:6379\"; return u; }\n")
		b.WriteString("  static String __resp(String... args) { try { var hp = __kvAddr().split(\":\"); var sock = new java.net.Socket(hp[0], Integer.parseInt(hp[1])); var out = sock.getOutputStream(); var sb = new StringBuilder(); sb.append(\"*\").append(args.length).append(\"\\r\\n\"); for (var a : args) { sb.append(\"$\").append(a.length()).append(\"\\r\\n\").append(a).append(\"\\r\\n\"); } out.write(sb.toString().getBytes()); out.flush(); var in = new java.io.BufferedReader(new java.io.InputStreamReader(sock.getInputStream())); var line = in.readLine(); String res = \"\"; if (line != null && !line.isEmpty()) { char t = line.charAt(0); if (t == '+') res = line.substring(1); else if (t == '$') { int n = Integer.parseInt(line.substring(1)); if (n >= 0) { char[] buf = new char[n]; int got = 0; while (got < n) { int r = in.read(buf, got, n - got); if (r < 0) break; got += r; } res = new String(buf, 0, got); } } } sock.close(); return res; } catch (Exception e) { return \"\"; } }\n")
		b.WriteString("  static V kvSetLive() { return fun(k -> fun(v -> fun(_u -> { __resp(\"SET\", __s2h(k), __s2h(v)); return v; }))); }\n")
		b.WriteString("  static V kvGetLive() { return fun(k -> fun(_u -> __h2s(__resp(\"GET\", __s2h(k))))); }\n")
		b.WriteString("  static V enqueueLive() { return fun(q -> fun(m -> fun(_u -> { __resp(\"LPUSH\", __s2h(q), __s2h(m)); return m; }))); }\n")
		b.WriteString("  static V dequeueLive() { return fun(q -> fun(_u -> __h2s(__resp(\"RPOP\", __s2h(q))))); }\n")
	}
	if usesForeign(p, "byteLen") {
		b.WriteString("  static V byteLen() { return fun(c -> new VNat(java.math.BigInteger.valueOf(__s2h(c).length()))); }\n")
	}
	if usesForeign(p, "splitOn") {
		b.WriteString("  static V splitOn() { return fun(sep -> fun(c -> { String data = __s2h(c); int sb = _nat(sep).intValue(); java.util.List<String> parts = new java.util.ArrayList<>(); int start = 0; for (int i = 0; i < data.length(); i++) { if (data.charAt(i) == sb) { parts.add(data.substring(start, i)); start = i + 1; } } parts.add(data.substring(start)); V lst = new VCtor(0, \"nil\", new V[]{unit()}); for (int i = parts.size() - 1; i >= 0; i--) lst = new VCtor(1, \"cons\", new V[]{unit(), __h2s(parts.get(i)), lst}); return lst; })); }\n")
	}
	if usesForeign(p, "jsonStrField") {
		b.WriteString("  static V jsonStrField() { return fun(field -> fun(doc -> { String fn = __s2h(field), ds = __s2h(doc); String needle = \"\\\"\" + fn + \"\\\"\"; V none = new VCtor(0, \"none\", new V[]{unit()}); int i = ds.indexOf(needle); if (i < 0) return none; int j = i + needle.length(); while (j < ds.length() && (ds.charAt(j) == ' ' || ds.charAt(j) == '\\t' || ds.charAt(j) == ':')) j++; if (j < ds.length() && ds.charAt(j) == '\"') { j++; int k = j; while (k < ds.length() && ds.charAt(k) != '\"') k++; return new VCtor(1, \"some\", new V[]{unit(), __h2s(ds.substring(j, k))}); } return none; })); }\n")
	}
	if usesForeign(p, "sqlQuote") {
		b.WriteString("  static V sqlQuote() { return fun(s -> { String in = __s2h(s); StringBuilder o = new StringBuilder(); o.append('\\''); for (int i = 0; i < in.length(); i++) { char ch = in.charAt(i); if (ch == '\\'') o.append('\\''); o.append(ch); } o.append('\\''); return __h2s(o.toString()); }); }\n")
	}
	if usesForeign(p, "foldLines") {
		b.WriteString("  static V foldLines() { return fun(_S -> fun(path -> fun(step -> fun(s0 -> fun(_u -> { byte[] data; try { data = java.nio.file.Files.readAllBytes(java.nio.file.Path.of(__s2h(path))); } catch (Exception e) { return s0; } String s = new String(data, java.nio.charset.StandardCharsets.ISO_8859_1); java.util.List<String> lines = new java.util.ArrayList<>(); int start = 0; for (int i = 0; i < s.length(); i++) { if (s.charAt(i) == '\\n') { lines.add(s.substring(start, i)); start = i + 1; } } lines.add(s.substring(start)); if (!lines.isEmpty() && lines.get(lines.size() - 1).isEmpty()) lines.remove(lines.size() - 1); V acc = s0; for (String ln : lines) acc = ap(ap(ap(step, acc), __h2s(ln)), unit()); return acc; }))))); }\n")
	}
	if usesForeign(p, "foldDir") {
		b.WriteString("  static void _foldwalk(String dir, String sfx, V step, V[] box) { java.io.File d = new java.io.File(dir); java.io.File[] ents = d.listFiles(); if (ents == null) return; java.util.Arrays.sort(ents, (a, b2) -> a.getName().compareTo(b2.getName())); for (java.io.File e : ents) { if (e.isDirectory()) _foldwalk(e.getPath(), sfx, step, box); else if (e.getPath().endsWith(sfx)) { try { byte[] data = java.nio.file.Files.readAllBytes(e.toPath()); box[0] = ap(ap(ap(step, box[0]), __h2s(new String(data, java.nio.charset.StandardCharsets.ISO_8859_1))), unit()); } catch (Exception ex) {} } } }\n")
		b.WriteString("  static V foldDir() { return fun(_S -> fun(dir -> fun(suf -> fun(step -> fun(s0 -> fun(_u -> { String sfx = __s2h(suf); V[] box = new V[]{s0}; _foldwalk(__s2h(dir), sfx, step, box); return box[0]; })))))); }\n")
	}
	// Write-stream handle family: mirrors the __socks/__sockId socket-handle pattern.
	// The opaque handle is a VNat token (a long id) into a static ConcurrentHashMap.
	if usesForeign(p, "openWrite") || usesForeign(p, "writeChunk") || usesForeign(p, "closeWrite") {
		b.WriteString("  static java.util.concurrent.ConcurrentHashMap<Long, java.io.OutputStream> __wh = new java.util.concurrent.ConcurrentHashMap<>();\n")
		b.WriteString("  static long __whId = 0;\n")
	}
	if usesForeign(p, "Handle") {
		b.WriteString("  static V Handle() { return UNIT; }\n")
	}
	if usesForeign(p, "openWrite") {
		b.WriteString("  static V openWrite() { return fun(path -> fun(_u -> { try { long id = ++__whId; java.io.OutputStream os = new java.io.FileOutputStream(__s2h(path)); __wh.put(id, os); return new VNat(java.math.BigInteger.valueOf(id)); } catch (Exception e) { return new VNat(java.math.BigInteger.ZERO); } })); }\n")
	}
	if usesForeign(p, "writeChunk") {
		b.WriteString("  static V writeChunk() { return fun(h -> fun(c -> fun(_u -> { try { java.io.OutputStream os = __wh.get(_nat(h).longValue()); if (os != null) os.write((__s2h(c) + \"\\n\").getBytes(java.nio.charset.StandardCharsets.ISO_8859_1)); } catch (Exception e) {} return h; }))); }\n")
	}
	if usesForeign(p, "closeWrite") {
		b.WriteString("  static V closeWrite() { return fun(h -> fun(_u -> { try { java.io.OutputStream os = __wh.remove(_nat(h).longValue()); if (os != null) os.close(); } catch (Exception e) {} return UNIT; })); }\n")
	}
	if usesForeign(p, "sortFile") {
		b.WriteString("  static V sortFile() { return fun(inp -> fun(outp -> fun(_u -> { try { byte[] data = java.nio.file.Files.readAllBytes(java.nio.file.Path.of(__s2h(inp))); String s = new String(data, java.nio.charset.StandardCharsets.ISO_8859_1); java.util.List<String> lines = new java.util.ArrayList<>(); int start = 0; for (int i = 0; i < s.length(); i++) { if (s.charAt(i) == '\\n') { lines.add(s.substring(start, i)); start = i + 1; } } lines.add(s.substring(start)); if (!lines.isEmpty() && lines.get(lines.size() - 1).isEmpty()) lines.remove(lines.size() - 1); java.util.Collections.sort(lines); StringBuilder o = new StringBuilder(); for (String ln : lines) { o.append(ln); o.append('\\n'); } java.nio.file.Files.write(java.nio.file.Path.of(__s2h(outp)), o.toString().getBytes(java.nio.charset.StandardCharsets.ISO_8859_1)); } catch (Exception e) { try { java.nio.file.Files.write(java.nio.file.Path.of(__s2h(outp)), new byte[0]); } catch (Exception e2) {} } return UNIT; }))); }\n")
	}
	if usesForeign(p, "dbApply") {
		b.WriteString("  static V dbApply() { return fun(db -> fun(sql -> fun(_u -> { try { new ProcessBuilder(\"sqlite3\", __s2h(db), \".read \" + __s2h(sql)).start().waitFor(); } catch (Exception e) {} return UNIT; }))); }\n")
	}
	for _, d := range p.Datas {
		if p.Nat != nil && d.ElimName == p.Nat.ElimName {
			emitNatJVM(&b, *p.Nat)
			continue
		}
		for _, c := range d.Ctors {
			emitCtorJVM(&b, c)
		}
		// B2 / R-IR: the eliminator is lowered to the shared IR once (LowerElim) and
		// emitted as an ordinary thunk — no eliminator-specific backend code.
		fmt.Fprintf(&b, "  static V %s() { return %s; }\n", jvmThunk(d.ElimName), em.expr(LowerElim(d), nil))
	}
	for _, def := range p.Defs {
		if p.Partials[def.Name] {
			// A partial is emitted in two parts so deep tail recursion runs flat:
			//   <P>_step()  — the body; a tail self-call evaluates to a VBounce that
			//                 re-enters _step (NOT the driver), so no frame accrues.
			//   <P>()       — the public entry: apply _step to the params, then
			//                 trampoline() the result ONCE. Callers see a value.
			stepName := jvmThunk(def.Name) + "_step"
			fmt.Fprintf(&b, "  static V %s() { return %s; }\n", stepName, em.expr(def.Body, nil))
			fmt.Fprintf(&b, "  static V %s() { return %s; }\n", jvmThunk(def.Name), em.exprPartialPublic(def.Body, stepName, nil))
			continue
		}
		fmt.Fprintf(&b, "  static V %s() { return %s; }\n", jvmThunk(def.Name), em.expr(def.Body, nil))
	}
	b.WriteString("  public static void main(String[] argv) throws Exception {\n")
	if p.Main != "" {
		mainExpr := jvmThunk(p.Main) + "()"
		if p.IOMain {
			// An IO main is a deferred world thunk (unit -> A): force by applying unit.
			mainExpr = fmt.Sprintf("ap(%s, unit())", mainExpr)
		}
		// Virtual thread: its growable stack survives deep erased recursion.
		fmt.Fprintf(&b, "    var _t = Thread.ofVirtual().start(() -> System.out.println(_show(%s)));\n", mainExpr)
		b.WriteString("    _t.join();\n")
	}
	b.WriteString("  }\n}\n")
	return TargetSource(b.String()), nil
}

// jvmCurry nests one-argument lambdas binding params in order; the body is the
// innermost expression with every param in scope. Java lambdas capture
// (effectively-final) outer locals by reference, so no clone juggling is needed.
func jvmCurry(params []string, body string) string {
	inner := body
	for i := len(params) - 1; i >= 0; i-- {
		inner = fmt.Sprintf("fun(%s -> %s)", params[i], inner)
	}
	return inner
}

func emitCtorJVM(b *strings.Builder, c CtorSpec) {
	params := make([]string, c.Arity)
	for i := range params {
		params[i] = fmt.Sprintf("a%d", i)
	}
	body := fmt.Sprintf("new VCtor(%d, %q, new V[]{%s})", c.Tag, c.Name, strings.Join(params, ", "))
	fmt.Fprintf(b, "  static V %s() { return %s; }\n", jvmThunk(c.Name), jvmCurry(params, body))
}

func emitNatJVM(b *strings.Builder, n NatSpec) {
	fmt.Fprintf(b, "  static V %s() { return new VNat(java.math.BigInteger.ZERO); }\n", jvmThunk(n.Zero))
	fmt.Fprintf(b, "  static V %s() { return fun(x -> new VNat(_nat(x).add(java.math.BigInteger.ONE))); }\n", jvmThunk(n.Succ))
	fmt.Fprintf(b, "  static V %s() { return %s; }\n", jvmThunk(n.ElimName),
		jvmCurry([]string{"m", "c0", "c1", "x"}, "_nat_elim(c0, c1, x)"))
}

type jvmEmitter struct {
	natElim string
	accel   map[string]core.NatOp
}

func (em *jvmEmitter) expr(t Ir, env []string) string {
	switch x := t.(type) {
	case IVar:
		if x.Idx < len(env) {
			return env[x.Idx]
		}
		return fmt.Sprintf("_v%d", x.Idx)
	case IGlobal:
		return jvmThunk(x.Name) + "()"
	case IForeign:
		// Host-linked accessor: the host defines `static V <name>() { … }`.
		return jvmName(x.Name) + "()"
	case IUnit:
		return "unit()"
	case ILit:
		// Native host literal marshalled at the FFI boundary (B4): a VInt scalar or
		// VStr (a dedicated record variant so strings never alias ints).
		if x.Kind == LitNat {
			// C7 / R-NUM: a compressed numeral deploys as a native VNat — the SAME
			// representation NatSpec gives nat (`VNat(ZERO)`, `+1`), arbitrary
			// precision (java.math.BigInteger), O(1) source instead of a succ-chain.
			return fmt.Sprintf("new VNat(new java.math.BigInteger(%q))", x.Nat)
		}
		if x.Kind == LitStr {
			return fmt.Sprintf("new VStr(%q)", x.Str)
		}
		if x.Kind == LitPtr {
			// Opaque host pointer (B4): a dedicated VPtr record so it never aliases
			// an int, and _show renders it as a fixed marker, not its handle.
			return fmt.Sprintf("new VPtr(%dL)", x.Int)
		}
		return fmt.Sprintf("new VInt(%dL)", x.Int)
	case ILam:
		n := freshJVM(x.Name, env)
		return fmt.Sprintf("fun(%s -> %s)", n, em.expr(x.Body, prepend(n, env)))
	case IApp:
		if out, ok := em.accelDispatch(x, env); ok {
			return out
		}
		if out, ok := em.natDispatch(x, env); ok {
			return out
		}
		return fmt.Sprintf("ap(%s, %s)", em.expr(x.Fn, env), em.expr(x.Arg, env))
	case ILet:
		// Java has no let-expression: a let is a beta redex, ap(fun(n -> body), val).
		n := freshJVM(x.Name, env)
		return fmt.Sprintf("ap(fun(%s -> %s), %s)", n, em.expr(x.Body, prepend(n, env)), em.expr(x.Val, env))
	case IPair:
		return fmt.Sprintf("new VPair(%s, %s)", em.expr(x.A, env), em.expr(x.B, env))
	case IFst:
		return fmt.Sprintf("_fst(%s)", em.expr(x.P, env))
	case ISnd:
		return fmt.Sprintf("_snd(%s)", em.expr(x.P, env))
	case IField:
		return fmt.Sprintf("_field(%s, %d)", em.expr(x.Scrut, env), x.Index)
	case IBounce:
		return em.exprBounce(x.Call, env)
	case ICase:
		scrut := em.expr(x.Scrut, env)
		var b strings.Builder
		fmt.Fprintf(&b, "(switch (_tag_of(%s)) {", scrut)
		for _, arm := range x.Arms {
			fmt.Fprintf(&b, " case %d -> %s;", arm.Tag, em.expr(arm.Body, env))
		}
		b.WriteString(" default -> _impossible(); })")
		return b.String()
	default:
		panic("codegen(jvm): unknown IR node")
	}
}

// accelDispatch emits a registered accel-op def on two args as native long
// arithmetic (C7 / R-NUM Decision 4) through the `_nat_add`/`_nat_mul`/
// `_nat_monus` runtime helpers (unbox VInt, compute, rebox). Monus is the
// saturating Peano monus (floors at 0). The long representation is the SAME
// NatSpec gives nat, so the result is interchangeable with the eliminator loop.
func (em *jvmEmitter) accelDispatch(app IApp, env []string) (string, bool) {
	op, a, b, ok := accelMatch(app, em.accel)
	if !ok {
		return "", false
	}
	ea, eb := em.expr(a, env), em.expr(b, env)
	switch op {
	case core.NatOpAdd:
		return fmt.Sprintf("_nat_add(%s, %s)", ea, eb), true
	case core.NatOpMul:
		return fmt.Sprintf("_nat_mul(%s, %s)", ea, eb), true
	case core.NatOpMonus:
		return fmt.Sprintf("_nat_monus(%s, %s)", ea, eb), true
	case core.NatOpDiv:
		return fmt.Sprintf("_nat_div(%s, %s)", ea, eb), true
	case core.NatOpMod:
		return fmt.Sprintf("_nat_mod(%s, %s)", ea, eb), true
	}
	return "", false
}

func (em *jvmEmitter) natDispatch(app IApp, env []string) (string, bool) {
	if em.natElim == "" {
		return "", false
	}
	var args []Ir
	t := Ir(app)
	for {
		a, ok := t.(IApp)
		if !ok {
			break
		}
		args = append([]Ir{a.Arg}, args...)
		t = a.Fn
	}
	g, ok := t.(IGlobal)
	if !ok || g.Name != em.natElim || len(args) < 4 {
		return "", false
	}
	c1lam, ok := args[2].(ILam)
	if !ok {
		return "", false
	}
	inner, ok := c1lam.Body.(ILam)
	if !ok || usesVar(inner.Body, 0) {
		return "", false
	}
	out := fmt.Sprintf("_nat_d(%s, %s, %s)",
		em.expr(args[1], env), em.expr(args[2], env), em.expr(args[3], env))
	for _, extra := range args[4:] {
		out = fmt.Sprintf("ap(%s, %s)", out, em.expr(extra, env))
	}
	return out, true
}

// exprBounce emits a tail self/sibling call as a VBounce that re-enters the head's
// _step entry (the body without a driver), so forcing it advances ONE iteration and
// returns the next bounce or value — the enclosing public driver loops without
// nesting (a flat host stack). The head of a marked bounce is always a partial.
func (em *jvmEmitter) exprBounce(call Ir, env []string) string {
	var args []Ir
	t := call
	for {
		app, ok := t.(IApp)
		if !ok {
			break
		}
		args = append([]Ir{app.Arg}, args...)
		t = app.Fn
	}
	g, ok := t.(IGlobal)
	if !ok {
		return em.expr(call, env) // not a recognizable spine; emit directly
	}
	s := jvmThunk(g.Name) + "_step()"
	for _, a := range args {
		s = fmt.Sprintf("ap(%s, %s)", s, em.expr(a, env))
	}
	return "new VBounce(() -> " + s + ")"
}

// exprPartialPublic emits a partial def's public entry: peel its parameter lambdas,
// apply the _step thunk to them, and trampoline() the result once.
func (em *jvmEmitter) exprPartialPublic(t Ir, stepThunk string, env []string) string {
	var params []string
	cur := t
	for {
		lam, ok := cur.(ILam)
		if !ok {
			break
		}
		n := freshJVM(lam.Name, env)
		params = append(params, n)
		env = prepend(n, env)
		cur = lam.Body
	}
	app := stepThunk + "()"
	for _, pn := range params {
		app = fmt.Sprintf("ap(%s, %s)", app, pn)
	}
	out := "trampoline(" + app + ")"
	for i := len(params) - 1; i >= 0; i-- {
		out = fmt.Sprintf("fun(%s -> %s)", params[i], out)
	}
	return out
}

func freshJVM(hint string, env []string) string {
	n := jvmName(hint)
	if n == "" || n == "_" {
		n = "_x"
	}
	if jvmReserved[n] {
		n = "_" + n
	}
	for {
		clash := false
		for _, e := range env {
			if e == n {
				clash = true
				break
			}
		}
		if !clash {
			return n
		}
		n += "_"
	}
}

// jvmThunk is the top-level thunk method name for a definition (suffix _d avoids
// clashing with the runtime helpers, which carry no suffix).
func jvmThunk(n string) string { return jvmName(n) + "_d" }

func jvmName(n string) string {
	if m, ok := jsOpNames[n]; ok {
		n = m
	} else if i := strings.IndexByte(n, '$'); i > 0 {
		if m, ok := jsOpNames[n[:i]]; ok {
			n = m + n[i:]
		}
	}
	n = strings.ReplaceAll(n, "'", "_")
	n = strings.ReplaceAll(n, "$", "_")
	n = strings.ReplaceAll(n, ".", "_")
	if n == "" {
		n = "_x"
	}
	if c := n[0]; c >= '0' && c <= '9' {
		n = "_" + n
	}
	return n
}

