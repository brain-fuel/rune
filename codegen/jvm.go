// JVM is a source-emitter backend (B-track / multi-backend) targeting JVM 25+:
// idiomatic modern Java over the SAME erased IR as JS/Python/Go/Rust/BEAM. A runtime
// value is a SEALED interface `V` with RECORD variants (unit / machine int / curried
// closure / tagged constructor / pair); application, projection, show, and the nat
// eliminator dispatch by PATTERN-MATCHING SWITCH over those records (Java 21+, final
// in 25); the entrypoint runs the show on a VIRTUAL THREAD (Java 21+, GA), whose
// growable stack absorbs the deep erased recursion the Rust/Python backends spawn a
// 512 MB OS-thread for. Each top-level definition is a static thunk method
// `name_d()`; references call it, so (mutual) recursion and eliminators are ordinary
// method calls — no cyclic-static problem. The single public class is `main`
// (so the file is `main.java`); javac/java come from a JDK 25 toolchain.
// Shadow-rule safe: a backend bug is wrong runtime output, never an unsound proof.
type JVM struct{}

func (JVM) Target() string { return "jvm" }

// Emit lowers a program to a self-contained Java 25 source file (javac-compilable).
func (JVM) Emit(p Program) (TargetSource, error) {
	em := &jvmEmitter{}
	if p.Nat != nil {
		em.natElim = p.Nat.ElimName
		em.accel = p.Nat.Ops
	}
	var b strings.Builder
	b.WriteString("// Generated by rune emit (jvm backend, Java 25+) — the erased shadow. Do not edit.\n")
	b.WriteString(jvmRuntime)
	b.WriteString("final class main {\n")
	b.WriteString(jvmHelpers)
	if usesQuot(p) {
		b.WriteString(jvmQuotRuntime)
	}
	if usesIO(p) {
		b.WriteString(jvmIORuntime())
	}
	// D6/D3: standard host bodies baked as `static V <name>()` methods (the foreign
	// accessor mechanism), gated by reference — the JVM counterparts of the js/py/go/
	// rust host bodies, so the f64 element type + a printing IO program run on Java 25.
	if usesForeign(p, "printNat") {
		b.WriteString("  static V printNat() { return fun(n -> fun(u -> { System.out.println(_nat(n)); return n; })); }\n")
	}
	if usesForeign(p, "Float") {
		b.WriteString("  static V Float() { return UNIT; }\n") // foreign type, erased
	}
	if usesForeign(p, "fromNat") {
		b.WriteString("  static V fromNat() { return fun(n -> new VFloat(_nat(n).doubleValue())); }\n")
	}
	if usesForeign(p, "fadd") {
		b.WriteString("  static V fadd() { return fun(a -> fun(b -> new VFloat(_float(a) + _float(b)))); }\n")
	}
	if usesForeign(p, "fsub") {
		b.WriteString("  static V fsub() { return fun(a -> fun(b -> new VFloat(_float(a) - _float(b)))); }\n")
	}
	if usesForeign(p, "fmul") {
		b.WriteString("  static V fmul() { return fun(a -> fun(b -> new VFloat(_float(a) * _float(b)))); }\n")
	}
	if usesForeign(p, "fdiv") {
		b.WriteString("  static V fdiv() { return fun(a -> fun(b -> new VFloat(_float(a) / _float(b)))); }\n")
	}
	if usesForeign(p, "fabsP") {
		b.WriteString("  static V fabsP() { return fun(x -> new VFloat(Math.abs(_float(x)))); }\n")
	}
	if usesForeign(p, "fsqrt") {
		b.WriteString("  static V fsqrt() { return fun(x -> new VFloat(Math.sqrt(_float(x)))); }\n")
	}
	if usesForeign(p, "fpow") {
		b.WriteString("  static V fpow() { return fun(a -> fun(e -> new VFloat(Math.pow(_float(a), _float(e))))); }\n")
	}
	if usesForeign(p, "floatToNat") {
		b.WriteString("  static V floatToNat() { return fun(x -> new VNat(java.math.BigInteger.valueOf((long) _float(x)))); }\n")
	}
	if usesForeign(p, "fleqN") {
		b.WriteString("  static V fleqN() { return fun(a -> fun(b -> new VNat(_float(a) <= _float(b) ? java.math.BigInteger.ONE : java.math.BigInteger.ZERO))); }\n")
	}
	if usesForeign(p, "dot2") {
		b.WriteString("  static V dot2() { return fun(a0 -> fun(a1 -> fun(b0 -> fun(b1 -> new VFloat(_float(a0)*_float(b0) + _float(a1)*_float(b1)))))); }\n")
	}
	// D5 / R-OTP: the JVM scheduler shim. Java 25 virtual threads CAN block on a queue,
	// so the actor blocks on a real receive — no CPS (the JS shim's problem). A Pid is a
	// VProc (a BlockingQueue mailbox + a CountDownLatch DOWN signal); goroutine-local
	// identity is a ThreadLocal. Mirrors goOTPRuntime/beamOTPRuntime on the JVM.
	if usesOTP(p) {
		b.WriteString("  static final ThreadLocal<VProc> __self = new ThreadLocal<>();\n")
		b.WriteString("  static VProc __me() { VProc p = __self.get(); if (p == null) { p = new VProc(new LinkedBlockingQueue<V>(), new CountDownLatch(1)); __self.set(p); } return p; }\n")
		b.WriteString("  static V Pid() { return fun(_M -> UNIT); }\n")
		b.WriteString("  static V primSelf() { return fun(_M -> fun(_u -> __me())); }\n")
		b.WriteString("  static V primSpawn() { return fun(_M -> fun(b -> fun(_u -> { VProc pr = new VProc(new LinkedBlockingQueue<V>(), new CountDownLatch(1)); Thread.ofVirtual().start(() -> { __self.set(pr); try { ap(ap(b, pr), unit()); } finally { pr.down().countDown(); } }); return pr; }))); }\n")
		b.WriteString("  static V primSend() { return fun(_M -> fun(p -> fun(x -> fun(_u -> { try { ((VProc)p).mb().put(x); } catch (InterruptedException e) { Thread.currentThread().interrupt(); } return UNIT; })))); }\n")
		b.WriteString("  static V primReceive() { return fun(_M -> fun(_u -> { try { return __me().mb().take(); } catch (InterruptedException e) { throw new RuntimeException(e); } })); }\n")
		b.WriteString("  static V primExit() { return fun(_M -> fun(p -> fun(_u -> { ((VProc)p).down().countDown(); return UNIT; }))); }\n")
		b.WriteString("  static V primMonitor() { return fun(_M -> fun(p -> fun(_u -> { try { ((VProc)p).down().await(); } catch (InterruptedException e) { Thread.currentThread().interrupt(); } return UNIT; }))); }\n")
	}
	for _, d := range p.Datas {
		if p.Nat != nil && d.ElimName == p.Nat.ElimName {
			emitNatJVM(&b, *p.Nat)
			continue
		}
		for _, c := range d.Ctors {
			emitCtorJVM(&b, c)
		}
		// B2 / R-IR: the eliminator is lowered to the shared IR once (LowerElim) and
		// emitted as an ordinary thunk — no eliminator-specific backend code.
		fmt.Fprintf(&b, "  static V %s() { return %s; }\n", jvmThunk(d.ElimName), em.expr(LowerElim(d), nil))
	}
	for _, def := range p.Defs {
		fmt.Fprintf(&b, "  static V %s() { return %s; }\n", jvmThunk(def.Name), em.expr(def.Body, nil))
	}
	b.WriteString("  public static void main(String[] argv) throws Exception {\n")
	if p.Main != "" {
		mainExpr := jvmThunk(p.Main) + "()"
		if p.IOMain {
			// An IO main is a deferred world thunk (unit -> A): force by applying unit.
			mainExpr = fmt.Sprintf("ap(%s, unit())", mainExpr)
		}
		// Virtual thread: its growable stack survives deep erased recursion.
		fmt.Fprintf(&b, "    var _t = Thread.ofVirtual().start(() -> System.out.println(_show(%s)));\n", mainExpr)
		b.WriteString("    _t.join();\n")
	}
	b.WriteString("  }\n}\n")
	return TargetSource(b.String()), nil
}

// jvmCurry nests one-argument lambdas binding params in order; the body is the
// innermost expression with every param in scope. Java lambdas capture
// (effectively-final) outer locals by reference, so no clone juggling is needed.
func jvmCurry(params []string, body string) string {
	inner := body
	for i := len(params) - 1; i >= 0; i-- {
		inner = fmt.Sprintf("fun(%s -> %s)", params[i], inner)
	}
	return inner
}

func emitCtorJVM(b *strings.Builder, c CtorSpec) {
	params := make([]string, c.Arity)
	for i := range params {
		params[i] = fmt.Sprintf("a%d", i)
	}
	body := fmt.Sprintf("new VCtor(%d, %q, new V[]{%s})", c.Tag, c.Name, strings.Join(params, ", "))
	fmt.Fprintf(b, "  static V %s() { return %s; }\n", jvmThunk(c.Name), jvmCurry(params, body))
}

func emitNatJVM(b *strings.Builder, n NatSpec) {
	fmt.Fprintf(b, "  static V %s() { return new VNat(java.math.BigInteger.ZERO); }\n", jvmThunk(n.Zero))
	fmt.Fprintf(b, "  static V %s() { return fun(x -> new VNat(_nat(x).add(java.math.BigInteger.ONE))); }\n", jvmThunk(n.Succ))
	fmt.Fprintf(b, "  static V %s() { return %s; }\n", jvmThunk(n.ElimName),
		jvmCurry([]string{"m", "c0", "c1", "x"}, "_nat_elim(c0, c1, x)"))
}

type jvmEmitter struct {
	natElim string
	accel   map[string]core.NatOp
}

func (em *jvmEmitter) expr(t Ir, env []string) string {
	switch x := t.(type) {
	case IVar:
		if x.Idx < len(env) {
			return env[x.Idx]
		}
		return fmt.Sprintf("_v%d", x.Idx)
	case IGlobal:
		return jvmThunk(x.Name) + "()"
	case IForeign:
		// Host-linked accessor: the host defines `static V <name>() { … }`.
		return jvmName(x.Name) + "()"
	case IUnit:
		return "unit()"
	case ILit:
		// Native host literal marshalled at the FFI boundary (B4): a VInt scalar or
		// VStr (a dedicated record variant so strings never alias ints).
		if x.Kind == LitNat {
			// C7 / R-NUM: a compressed numeral deploys as a native VNat — the SAME
			// representation NatSpec gives nat (`VNat(ZERO)`, `+1`), arbitrary
			// precision (java.math.BigInteger), O(1) source instead of a succ-chain.
			return fmt.Sprintf("new VNat(new java.math.BigInteger(%q))", x.Nat)
		}
		if x.Kind == LitStr {
			return fmt.Sprintf("new VStr(%q)", x.Str)
		}
		if x.Kind == LitPtr {
			// Opaque host pointer (B4): a dedicated VPtr record so it never aliases
			// an int, and _show renders it as a fixed marker, not its handle.
			return fmt.Sprintf("new VPtr(%dL)", x.Int)
		}
		return fmt.Sprintf("new VInt(%dL)", x.Int)
	case ILam:
		n := freshJVM(x.Name, env)
		return fmt.Sprintf("fun(%s -> %s)", n, em.expr(x.Body, prepend(n, env)))
	case IApp:
		if out, ok := em.accelDispatch(x, env); ok {
			return out
		}
		if out, ok := em.natDispatch(x, env); ok {
			return out
		}
		return fmt.Sprintf("ap(%s, %s)", em.expr(x.Fn, env), em.expr(x.Arg, env))
	case ILet:
		// Java has no let-expression: a let is a beta redex, ap(fun(n -> body), val).
		n := freshJVM(x.Name, env)
		return fmt.Sprintf("ap(fun(%s -> %s), %s)", n, em.expr(x.Body, prepend(n, env)), em.expr(x.Val, env))
	case IPair:
		return fmt.Sprintf("new VPair(%s, %s)", em.expr(x.A, env), em.expr(x.B, env))
	case IFst:
		return fmt.Sprintf("_fst(%s)", em.expr(x.P, env))
	case ISnd:
		return fmt.Sprintf("_snd(%s)", em.expr(x.P, env))
	case IField:
		return fmt.Sprintf("_field(%s, %d)", em.expr(x.Scrut, env), x.Index)
	case ICase:
		scrut := em.expr(x.Scrut, env)
		var b strings.Builder
		fmt.Fprintf(&b, "(switch (_tag_of(%s)) {", scrut)
		for _, arm := range x.Arms {
			fmt.Fprintf(&b, " case %d -> %s;", arm.Tag, em.expr(arm.Body, env))
		}
		b.WriteString(" default -> _impossible(); })")
		return b.String()
	default:
		panic("codegen(jvm): unknown IR node")
	}
}

// accelDispatch emits a registered accel-op def on two args as native long
// arithmetic (C7 / R-NUM Decision 4) through the `_nat_add`/`_nat_mul`/
// `_nat_monus` runtime helpers (unbox VInt, compute, rebox). Monus is the
// saturating Peano monus (floors at 0). The long representation is the SAME
// NatSpec gives nat, so the result is interchangeable with the eliminator loop.
func (em *jvmEmitter) accelDispatch(app IApp, env []string) (string, bool) {
	op, a, b, ok := accelMatch(app, em.accel)
	if !ok {
		return "", false
	}
	ea, eb := em.expr(a, env), em.expr(b, env)
	switch op {
	case core.NatOpAdd:
		return fmt.Sprintf("_nat_add(%s, %s)", ea, eb), true
	case core.NatOpMul:
		return fmt.Sprintf("_nat_mul(%s, %s)", ea, eb), true
	case core.NatOpMonus:
		return fmt.Sprintf("_nat_monus(%s, %s)", ea, eb), true
	}
	return "", false
}

func (em *jvmEmitter) natDispatch(app IApp, env []string) (string, bool) {
	if em.natElim == "" {
		return "", false
	}
	var args []Ir
	t := Ir(app)
	for {
		a, ok := t.(IApp)
		if !ok {
			break
		}
		args = append([]Ir{a.Arg}, args...)
		t = a.Fn
	}
	g, ok := t.(IGlobal)
	if !ok || g.Name != em.natElim || len(args) < 4 {
		return "", false
	}
	c1lam, ok := args[2].(ILam)
	if !ok {
		return "", false
	}
	inner, ok := c1lam.Body.(ILam)
	if !ok || usesVar(inner.Body, 0) {
		return "", false
	}
	out := fmt.Sprintf("_nat_d(%s, %s, %s)",
		em.expr(args[1], env), em.expr(args[2], env), em.expr(args[3], env))
	for _, extra := range args[4:] {
		out = fmt.Sprintf("ap(%s, %s)", out, em.expr(extra, env))
	}
	return out, true
}

func freshJVM(hint string, env []string) string {
	n := jvmName(hint)
	if n == "" || n == "_" {
		n = "_x"
	}
	if jvmReserved[n] {
		n = "_" + n
	}
	for {
		clash := false
		for _, e := range env {
			if e == n {
				clash = true
				break
			}
		}
		if !clash {
			return n
		}
		n += "_"
	}
}

// jvmThunk is the top-level thunk method name for a definition (suffix _d avoids
// clashing with the runtime helpers, which carry no suffix).
func jvmThunk(n string) string { return jvmName(n) + "_d" }

func jvmName(n string) string {
	if m, ok := jsOpNames[n]; ok {
		n = m
	} else if i := strings.IndexByte(n, '$'); i > 0 {
		if m, ok := jsOpNames[n[:i]]; ok {
			n = m + n[i:]
		}
	}
	n = strings.ReplaceAll(n, "'", "_")
	n = strings.ReplaceAll(n, "$", "_")
	n = strings.ReplaceAll(n, ".", "_")
	if n == "" {
		n = "_x"
	}
	if c := n[0]; c >= '0' && c <= '9' {
		n = "_" + n
	}
	return n
}

