package codegen

import (
	"fmt"
	"strings"

	"goforge.dev/rune/v3/core"
)

// Py is a second backend (B-track / multi-backend): plain Python 3 over the SAME
// erased IR as the JS backend. Everything is curried one-argument lambdas;
// constructors build {"tag","name","args"} dicts; eliminators are Python defs
// with chained-conditional dispatch and recursive calls for the induction
// hypotheses; the erased token is None; nat is int. The output runs under
// python3 with no dependencies. Its purpose is cross-backend conformance: the
// same program must observe equal results on JS and Python (the shadow rule —
// codegen mutates only its own output, never the core).
type Py struct{}

func (Py) Target() string { return "py" }

// Emit lowers a program to a self-contained Python 3 source file.
func (Py) Emit(p Program) (TargetSource, error) {
	em := &pyEmitter{}
	if p.Nat != nil {
		em.natElim = p.Nat.ElimName
		em.accel = p.Nat.Ops
	}
	var b strings.Builder
	b.WriteString("# Generated by rune emit (py backend) — the erased shadow. Do not edit.\n")
	b.WriteString("import sys\nsys.setrecursionlimit(1000000)\n")
	b.WriteString("_unit = None\n")
	b.WriteString(pyNatElimRuntime)
	if usesQuot(p) {
		b.WriteString(pyQuotRuntime)
	}
	if usesIO(p) {
		b.WriteString(pyIORuntime)
	}
	// D6 / R-EFFECT: bake the standard OS/IO host bodies when referenced.
	if usesForeign(p, "printNat") {
		b.WriteString("def printNat():\n    return lambda n: lambda _u: (print(n), n)[1]\n")
	}
	// Phase 0 — real byte strings (Bin): a Python list of byte ints; nats are ints.
	if usesBin(p) {
		b.WriteString("def binEmpty():\n    return []\n")
		b.WriteString("def binCons():\n    return lambda c: lambda b: [c & 255] + b\n")
		b.WriteString("def binLen():\n    return lambda b: len(b)\n")
		b.WriteString("def binAt():\n    return lambda b: lambda i: (b[i] if 0 <= i < len(b) else 0)\n")
		b.WriteString("def __binshow(b):\n    s = '\"'\n    for x in b:\n        if 0x20 <= x < 0x7f and x != 0x22 and x != 0x5c:\n            s += chr(x)\n        else:\n            s += '\\\\x%02x' % x\n    return s + '\"'\n")
		b.WriteString("def printBin():\n    return lambda b: lambda _u: (print(__binshow(b)), _unit)[1]\n")
	}
	// Phase 1 — uniform socket FFI: handles are Python socket objects; payloads are
	// Bin (list of byte ints). Blocking (Python sockets are synchronous).
	if usesNet(p) {
		b.WriteString("def _sockConnect(host, port):\n    import socket\n    s = socket.socket(socket.AF_INET, socket.SOCK_STREAM)\n    s.connect((bytes(host).decode('latin1'), port))\n    return s\n")
		b.WriteString("def sockConnect():\n    return lambda host: lambda port: lambda _u: _sockConnect(host, port)\n")
		b.WriteString("def sockWrite():\n    return lambda c: lambda data: lambda _u: c.send(bytes(data))\n")
		b.WriteString("def sockRead():\n    return lambda c: lambda n: lambda _u: list(c.recv(n))\n")
		b.WriteString("def sockClose():\n    return lambda c: lambda _u: (c.close(), _unit)[1]\n")
		b.WriteString("def _sockListen(port):\n    import socket\n    s = socket.socket(socket.AF_INET, socket.SOCK_STREAM)\n    s.setsockopt(socket.SOL_SOCKET, socket.SO_REUSEADDR, 1)\n    s.bind(('127.0.0.1', port))\n    s.listen(1)\n    return s\n")
		b.WriteString("def sockListen():\n    return lambda port: lambda _u: _sockListen(port)\n")
		b.WriteString("def sockAccept():\n    return lambda l: lambda _u: l.accept()[0]\n")
	}
	// Phase 1 — expanded OS/filesystem layer (Bin-native paths + content).
	if usesFS(p) {
		b.WriteString("def _fsWrite(path, content):\n    try:\n        f = open(bytes(path).decode('latin1'), 'wb'); f.write(bytes(content)); f.close(); return len(content)\n    except Exception:\n        return 0\n")
		b.WriteString("def fsWrite():\n    return lambda path: lambda content: lambda _u: _fsWrite(path, content)\n")
		b.WriteString("def _fsRead(path):\n    try:\n        f = open(bytes(path).decode('latin1'), 'rb'); d = f.read(); f.close(); return list(d)\n    except Exception:\n        return []\n")
		b.WriteString("def fsRead():\n    return lambda path: lambda _u: _fsRead(path)\n")
		b.WriteString("def fsExists():\n    import os\n    return lambda path: lambda _u: (1 if os.path.exists(bytes(path).decode('latin1')) else 0)\n")
		b.WriteString("def _fsRemove(path):\n    import os\n    try:\n        os.remove(bytes(path).decode('latin1')); return 1\n    except Exception:\n        return 0\n")
		b.WriteString("def fsRemove():\n    return lambda path: lambda _u: _fsRemove(path)\n")
		b.WriteString("def _fsMkdir(path):\n    import os\n    try:\n        os.makedirs(bytes(path).decode('latin1'), exist_ok=True); return 1\n    except Exception:\n        return 0\n")
		b.WriteString("def fsMkdir():\n    return lambda path: lambda _u: _fsMkdir(path)\n")
	}
	// Phase 1 — os/exec: run program with one arg, capture stdout.
	if usesProc(p) {
		b.WriteString("def _procRun(prog, arg):\n    import subprocess\n    try:\n        r = subprocess.run([bytes(prog).decode('latin1'), bytes(arg).decode('latin1')], capture_output=True)\n        return list(r.stdout)\n    except Exception:\n        return []\n")
		b.WriteString("def procRun():\n    return lambda prog: lambda arg: lambda _u: _procRun(prog, arg)\n")
	}
	// Phase 3 — crypto: host sha256 (pure).
	if usesCrypto(p) {
		b.WriteString("def sha256():\n    import hashlib\n    return lambda data: list(hashlib.sha256(bytes(data)).digest())\n")
	}
	// Phase 3 — TLS: HTTPS GET, return response body (cert verification skipped).
	if usesTLS(p) {
		b.WriteString("def _tlsGet(host, port, path):\n    import ssl, http.client\n    try:\n        ctx = ssl._create_unverified_context()\n        c = http.client.HTTPSConnection(bytes(host).decode('latin1'), port, context=ctx)\n        c.request('GET', bytes(path).decode('latin1'))\n        r = c.getresponse(); d = r.read(); c.close(); return list(d)\n    except Exception:\n        return []\n")
		b.WriteString("def tlsGet():\n    return lambda host: lambda port: lambda path: lambda _u: _tlsGet(host, port, path)\n")
	}
	if usesForeign(p, "getNat") {
		b.WriteString("def getNat():\n    import sys\n    return lambda _u: int(sys.stdin.readline())\n")
	}
	if usesForeign(p, "timeNanos") {
		b.WriteString("def timeNanos():\n    import time\n    return lambda _u: time.time_ns()\n")
	}
	if usesForeign(p, "readLineCode") {
		b.WriteString("def readLineCode():\n    import sys\n    def t(_u):\n        s = sys.stdin.readline().rstrip('\\n')\n        n = 1\n        for ch in reversed(s):\n            n = n * 256 + ord(ch)\n        return n\n    return t\n")
	}
	// D6 net/fs: the packed-String codec + env/file host bodies, over bare Nat codes.
	if usesFileEnv(p) {
		b.WriteString("def __s2h(n):\n    s = ''\n    while n > 1:\n        s += chr(n % 256)\n        n //= 256\n    return s\n")
		b.WriteString("def __h2s(v):\n    n = 1\n    for ch in reversed(v):\n        n = n * 256 + ord(ch)\n    return n\n")
	}
	if usesForeign(p, "getEnvCode") {
		b.WriteString("def getEnvCode():\n    import os\n    return lambda c: lambda _u: __h2s(os.environ.get(__s2h(c), ''))\n")
	}
	if usesForeign(p, "readFileCode") {
		b.WriteString("def readFileCode():\n    def f(c):\n        def t(_u):\n            try:\n                with open(__s2h(c), 'r') as h:\n                    return __h2s(h.read())\n            except Exception:\n                return 1\n        return t\n    return f\n")
	}
	if usesForeign(p, "writeFileCode") {
		b.WriteString("def writeFileCode():\n    def f(p2):\n        def g(c):\n            def t(_u):\n                with open(__s2h(p2), 'w') as h:\n                    h.write(__s2h(c))\n                return c\n            return t\n        return g\n    return f\n")
	}
	if usesForeign(p, "printStrCode") {
		b.WriteString("def printStrCode():\n    return lambda c: lambda _u: (print(__s2h(c)), c)[1]\n")
	}
	// D6 argv + process.
	if usesForeign(p, "argCountCode") {
		b.WriteString("def argCountCode():\n    import sys\n    return lambda _u: len(sys.argv) - 1\n")
	}
	if usesForeign(p, "argAtCode") {
		b.WriteString("def argAtCode():\n    import sys\n    return lambda i: lambda _u: (__h2s(sys.argv[i + 1]) if i + 1 < len(sys.argv) else 1)\n")
	}
	if usesForeign(p, "exitWith") {
		b.WriteString("def exitWith():\n    import sys\n    return lambda n: lambda _u: sys.exit(n)\n")
	}
	// D3 machine floats (f64) + the BLAS dot kernel — native float arithmetic.
	// `Float` is a foreign type surviving erasure as ok/err's type arg (runtime-irrelevant).
	if usesForeign(p, "Float") {
		b.WriteString("def Float():\n    return None\n")
	}
	// E4 / wavelet — the local in-process KV backend (a module-global dict keyed by
	// the bare Nat code), so a kv program RUNS unaided. An absent key reads as 0.
	if usesForeign(p, "kvPutCode") || usesForeign(p, "kvGetCode") || usesForeign(p, "kvDelCode") {
		b.WriteString("__kv = {}\n")
	}
	if usesForeign(p, "kvPutCode") {
		b.WriteString("def kvPutCode():\n    return lambda k: lambda v: lambda _u: (__kv.__setitem__(k, v), v)[1]\n")
	}
	if usesForeign(p, "kvGetCode") {
		b.WriteString("def kvGetCode():\n    return lambda k: lambda _u: __kv.get(k, 0)\n")
	}
	if usesForeign(p, "kvDelCode") {
		b.WriteString("def kvDelCode():\n    return lambda k: lambda _u: (__kv.pop(k, 0), 0)[1]\n")
	}
	// E4 / wavelet — the local OBJECT store (same dict shape as kv, distinct global).
	if usesForeign(p, "objPutCode") || usesForeign(p, "objGetCode") || usesForeign(p, "objDelCode") {
		b.WriteString("__obj = {}\n")
	}
	if usesForeign(p, "objPutCode") {
		b.WriteString("def objPutCode():\n    return lambda k: lambda v: lambda _u: (__obj.__setitem__(k, v), v)[1]\n")
	}
	if usesForeign(p, "objGetCode") {
		b.WriteString("def objGetCode():\n    return lambda k: lambda _u: __obj.get(k, 0)\n")
	}
	if usesForeign(p, "objDelCode") {
		b.WriteString("def objDelCode():\n    return lambda k: lambda _u: (__obj.pop(k, 0), 0)[1]\n")
	}
	// E4 / wavelet — the local QUEUE (a FIFO list per queue code).
	if usesForeign(p, "enqueueCode") || usesForeign(p, "dequeueCode") {
		b.WriteString("__q = {}\n")
	}
	if usesForeign(p, "enqueueCode") {
		b.WriteString("def enqueueCode():\n    return lambda q: lambda m: lambda _u: (__q.setdefault(q, []).append(m), m)[1]\n")
	}
	if usesForeign(p, "dequeueCode") {
		b.WriteString("def dequeueCode():\n    return lambda q: lambda _u: (__q[q].pop(0) if __q.get(q) else 0)\n")
	}
	if usesForeign(p, "fromNat") {
		b.WriteString("def fromNat():\n    return lambda n: float(n)\n")
	}
	if usesForeign(p, "fadd") {
		b.WriteString("def fadd():\n    return lambda a: lambda b: a + b\n")
	}
	if usesForeign(p, "fsub") {
		b.WriteString("def fsub():\n    return lambda a: lambda b: a - b\n")
	}
	if usesForeign(p, "fmul") {
		b.WriteString("def fmul():\n    return lambda a: lambda b: a * b\n")
	}
	if usesForeign(p, "fdiv") {
		b.WriteString("def fdiv():\n    return lambda a: lambda b: a / b\n")
	}
	if usesForeign(p, "fabsP") {
		b.WriteString("def fabsP():\n    return lambda x: abs(x)\n")
	}
	if usesForeign(p, "floatToNat") {
		b.WriteString("def floatToNat():\n    return lambda x: int(x)\n")
	}
	if usesForeign(p, "fleqN") {
		b.WriteString("def fleqN():\n    return lambda a: lambda b: (1 if a <= b else 0)\n")
	}
	if usesForeign(p, "fsqrt") {
		b.WriteString("def fsqrt():\n    import math\n    return lambda x: math.sqrt(x)\n")
	}
	if usesForeign(p, "fpow") {
		b.WriteString("def fpow():\n    import math\n    return lambda b: lambda e: math.pow(b, e)\n")
	}
	if usesForeign(p, "dot2") {
		b.WriteString("def dot2():\n    return lambda a0: lambda a1: lambda b0: lambda b1: a0 * b0 + a1 * b1\n")
	}
	// Arbitrary-length dot: walk two Rune FLists (fcons = tag 1, head args[0], tail
	// args[1]) summing products — the portable reference (native backends use cblas).
	if usesForeign(p, "dotList") {
		b.WriteString("def dotList():\n    def _dl(xs, ys):\n        s = 0.0\n        while xs[\"tag\"] == 1 and ys[\"tag\"] == 1:\n            s += xs[\"args\"][0] * ys[\"args\"][0]\n            xs = xs[\"args\"][1]; ys = ys[\"args\"][1]\n        return s\n    return lambda xs: lambda ys: _dl(xs, ys)\n")
	}
	// D4 interop: npDot binds REAL NumPy on the py backend — marshal each Rune FList
	// into an np.array and call np.dot. The Rune tolerance contract guards the result,
	// so the third-party library is checked, never trusted blindly.
	if usesForeign(p, "npDot") {
		b.WriteString("def npDot():\n    import numpy as np\n    def _np(xs):\n        a = []; t = xs\n        while t[\"tag\"] == 1: a.append(t[\"args\"][0]); t = t[\"args\"][1]\n        return np.array(a, dtype=float)\n    return lambda xs: lambda ys: float(np.dot(_np(xs), _np(ys)))\n")
	}
	// D4 interop: npMean binds REAL numpy.mean on the py backend (no BLAS needed).
	if usesForeign(p, "npMean") {
		b.WriteString("def npMean():\n    import numpy as np\n    def _m(xs):\n        a = []; t = xs\n        while t[\"tag\"] == 1: a.append(t[\"args\"][0]); t = t[\"args\"][1]\n        return float(np.mean(np.array(a, dtype=float))) if a else 0.0\n    return lambda xs: _m(xs)\n")
	}
	// D4 PLOTTING reach (telos 3): plotSave renders the FList as a line plot via REAL
	// matplotlib (Agg backend, headless) and writes wavelet_plot.png, returning the
	// point count. Plotting is the py-specific reach (no cross-backend artifact); the
	// IO Nat result is the observable the other backends floor.
	if usesForeign(p, "plotSave") {
		b.WriteString("def plotSave():\n    def _p(xs):\n        import matplotlib; matplotlib.use(\"Agg\")\n        import matplotlib.pyplot as plt\n        a = []; t = xs\n        while t[\"tag\"] == 1: a.append(t[\"args\"][0]); t = t[\"args\"][1]\n        plt.figure(); plt.plot(a); plt.savefig(\"wavelet_plot.png\"); plt.close()\n        return len(a)\n    return lambda xs: lambda _u: _p(xs)\n")
	}
	// D4 ML pipeline (ch473): scatterLine scatters (xs,ys) and overlays a fit line (xs,yhat)
	// via REAL matplotlib, writes wavelet_fit.png, returns the point count. The plot reach of
	// the fit.rune pipeline (py-specific, like plotSave); the IO Nat count is the observable.
	if usesForeign(p, "scatterLine") {
		b.WriteString("def scatterLine():\n    def _fl(xs):\n        a = []; t = xs\n        while t[\"tag\"] == 1: a.append(t[\"args\"][0]); t = t[\"args\"][1]\n        return a\n    def _s(xs, ys, yh):\n        import matplotlib; matplotlib.use(\"Agg\")\n        import matplotlib.pyplot as plt\n        ax = _fl(xs); ay = _fl(ys); af = _fl(yh)\n        plt.figure(); plt.scatter(ax, ay); plt.plot(ax, af); plt.savefig(\"wavelet_fit.png\"); plt.close()\n        return len(ax)\n    return lambda xs: lambda ys: lambda yh: lambda _u: _s(xs, ys, yh)\n")
	}
	// D4 interop: npNorm binds REAL numpy.linalg.norm on the py backend (Euclidean L2).
	if usesForeign(p, "npNorm") {
		b.WriteString("def npNorm():\n    import numpy as np\n    def _n(xs):\n        a = []; t = xs\n        while t[\"tag\"] == 1: a.append(t[\"args\"][0]); t = t[\"args\"][1]\n        return float(np.linalg.norm(np.array(a, dtype=float))) if a else 0.0\n    return lambda xs: _n(xs)\n")
	}
	// D4 interop: npMax binds REAL numpy.max on the py backend (order reduction).
	if usesForeign(p, "npMax") {
		b.WriteString("def npMax():\n    import numpy as np\n    def _x(xs):\n        a = []; t = xs\n        while t[\"tag\"] == 1: a.append(t[\"args\"][0]); t = t[\"args\"][1]\n        return float(np.max(np.array(a, dtype=float))) if a else 0.0\n    return lambda xs: _x(xs)\n")
	}
	// D4 interop: npVar binds REAL numpy.var on the py backend (population variance).
	if usesForeign(p, "npVar") {
		b.WriteString("def npVar():\n    import numpy as np\n    def _v(xs):\n        a = []; t = xs\n        while t[\"tag\"] == 1: a.append(t[\"args\"][0]); t = t[\"args\"][1]\n        return float(np.var(np.array(a, dtype=float))) if a else 0.0\n    return lambda xs: _v(xs)\n")
	}
	// D4 interop: npMatSum binds REAL numpy matmul on py — reshape the flat row-major
	// FLists into 2-D np.arrays, multiply with @, sum. This proves the 2-D marshalling
	// boundary works THROUGH numpy (the signature linalg op).
	if usesForeign(p, "npMatSum") {
		b.WriteString("def npMatSum():\n    import numpy as np\n    def _np(xs):\n        a = []; t = xs\n        while t[\"tag\"] == 1: a.append(t[\"args\"][0]); t = t[\"args\"][1]\n        return a\n    def _g(m, k, n, A, B):\n        M, K, N = int(m), int(k), int(n)\n        a = np.array(_np(A), dtype=float).reshape(M, K)\n        b = np.array(_np(B), dtype=float).reshape(K, N)\n        return float((a @ b).sum())\n    return lambda m: lambda k: lambda n: lambda A: lambda B: _g(m, k, n, A, B)\n")
	}
	// Matrix product sum: flat row-major A (m×k), B (k×n); sum all entries of A·B — the
	// portable triple-loop reference (native backends use cblas_dgemm).
	if usesForeign(p, "gemmSum") {
		b.WriteString("def gemmSum():\n    def _g(m, k, n, A, B):\n        a = []; t = A\n        while t[\"tag\"] == 1: a.append(t[\"args\"][0]); t = t[\"args\"][1]\n        b2 = []; t = B\n        while t[\"tag\"] == 1: b2.append(t[\"args\"][0]); t = t[\"args\"][1]\n        M, K, N = int(m), int(k), int(n); s = 0.0\n        for i in range(M):\n            for j in range(N):\n                for l in range(K):\n                    s += a[i * K + l] * b2[l * N + j]\n        return s\n    return lambda m: lambda k: lambda n: lambda A: lambda B: _g(m, k, n, A, B)\n")
	}
	for _, d := range p.Datas {
		if p.Nat != nil && d.ElimName == p.Nat.ElimName {
			emitNatPy(&b, *p.Nat)
			continue
		}
		for _, c := range d.Ctors {
			emitCtorPy(&b, c)
		}
		// B2 / R-IR: the eliminator is lowered to the shared IR once (LowerElim)
		// and emitted as an ordinary binding — no eliminator-specific backend code.
		fmt.Fprintf(&b, "%s = %s\n", pyName(d.ElimName), em.expr(LowerElim(d), nil))
	}
	for _, def := range p.Defs {
		fmt.Fprintf(&b, "%s = %s\n", pyName(def.Name), em.expr(def.Body, nil))
	}
	if p.Main != "" {
		b.WriteString(pyShowRuntime)
		mainExpr := pyName(p.Main)
		if p.IOMain {
			// An IO main is a deferred world thunk (unit -> A): force it before show.
			mainExpr += "(_unit)"
		}
		fmt.Fprintf(&b, "print(_show(%s))\n", mainExpr)
	}
	return TargetSource(b.String()), nil
}

func emitCtorPy(b *strings.Builder, c CtorSpec) {
	fmt.Fprintf(b, "%s = ", pyName(c.Name))
	for i := 0; i < c.Arity; i++ {
		fmt.Fprintf(b, "lambda a%d: ", i)
	}
	args := make([]string, c.Arity)
	for i := range args {
		args[i] = fmt.Sprintf("a%d", i)
	}
	fmt.Fprintf(b, "{\"tag\": %d, \"name\": %q, \"args\": [%s]}\n", c.Tag, c.Name, strings.Join(args, ", "))
}

func emitNatPy(b *strings.Builder, n NatSpec) {
	fmt.Fprintf(b, "%s = 0\n", pyName(n.Zero))
	fmt.Fprintf(b, "%s = lambda x: x + 1\n", pyName(n.Succ))
	fmt.Fprintf(b, "%s = lambda m: lambda c0: lambda c1: lambda x: _natElim(c0, c1, x)\n", pyName(n.ElimName))
}

type pyEmitter struct {
	natElim string
	accel   map[string]core.NatOp
}

func (em *pyEmitter) expr(t Ir, env []string) string {
	switch x := t.(type) {
	case IVar:
		if x.Idx < len(env) {
			return env[x.Idx]
		}
		return fmt.Sprintf("_v%d", x.Idx)
	case IGlobal:
		return pyName(x.Name)
	case IForeign:
		// Host-linked accessor: the host defines `def <name>(): return …`.
		return pyName(x.Name) + "()"
	case IUnit:
		return "_unit"
	case ILit:
		// Native host literal marshalled at the FFI boundary (B4): a Python int or
		// str literal.
		if x.Kind == LitNat {
			// C7 / R-NUM: a compressed numeral deploys as a native Python int — the
			// SAME representation NatSpec gives nat (`0`/`x + 1`), arbitrary
			// precision, O(1) source instead of a succ-chain.
			return x.Nat
		}
		if x.Kind == LitStr {
			return fmt.Sprintf("%q", x.Str)
		}
		if x.Kind == LitPtr {
			// Opaque host pointer: boxed in a dict so it never aliases an int, and
			// _show renders it as a fixed marker rather than its handle.
			return fmt.Sprintf("{\"ptr\": %d}", x.Int)
		}
		return fmt.Sprintf("%d", x.Int)
	case ILam:
		n := freshPy(x.Name, env)
		return fmt.Sprintf("(lambda %s: %s)", n, em.expr(x.Body, prepend(n, env)))
	case IApp:
		if out, ok := em.accelDispatch(x, env); ok {
			return out
		}
		if out, ok := em.natDispatch(x, env); ok {
			return out
		}
		return fmt.Sprintf("%s(%s)", em.expr(x.Fn, env), em.expr(x.Arg, env))
	case ILet:
		n := freshPy(x.Name, env)
		return fmt.Sprintf("(lambda %s: %s)(%s)", n, em.expr(x.Body, prepend(n, env)), em.expr(x.Val, env))
	case IPair:
		return fmt.Sprintf("(%s, %s)", em.expr(x.A, env), em.expr(x.B, env))
	case IFst:
		return fmt.Sprintf("(%s)[0]", em.expr(x.P, env))
	case ISnd:
		return fmt.Sprintf("(%s)[1]", em.expr(x.P, env))
	case IField:
		return fmt.Sprintf("(%s)[\"args\"][%d]", em.expr(x.Scrut, env), x.Index)
	case IBounce:
		return em.expr(x.Call, env)
	case ICase:
		// Chained conditional expression on the scrutinee tag (Python has no
		// statement switch in an expression position).
		scrut := em.expr(x.Scrut, env)
		var b strings.Builder
		b.WriteString("(")
		for _, arm := range x.Arms {
			fmt.Fprintf(&b, "%s if (%s)[\"tag\"] == %d else ", em.expr(arm.Body, env), scrut, arm.Tag)
		}
		b.WriteString("_impossible())")
		return b.String()
	default:
		panic("codegen(py): unknown IR node")
	}
}

// accelDispatch emits a registered accel-op def on two args as native Python int
// arithmetic (C7 / R-NUM Decision 4): add → `(a + b)`, mul → `(a * b)`, monus →
// saturating `(a - b if a > b else 0)` (Peano monus floors at 0). Python ints are
// arbitrary precision, the SAME representation NatSpec gives nat.
func (em *pyEmitter) accelDispatch(app IApp, env []string) (string, bool) {
	op, a, b, ok := accelMatch(app, em.accel)
	if !ok {
		return "", false
	}
	ea, eb := em.expr(a, env), em.expr(b, env)
	switch op {
	case core.NatOpAdd:
		return fmt.Sprintf("(%s + %s)", ea, eb), true
	case core.NatOpMul:
		return fmt.Sprintf("(%s * %s)", ea, eb), true
	case core.NatOpMonus:
		return fmt.Sprintf("((%s) - (%s) if (%s) > (%s) else 0)", ea, eb, ea, eb), true
	case core.NatOpDiv:
		return fmt.Sprintf("((%s) // (%s) if (%s) != 0 else 0)", ea, eb, eb), true
	case core.NatOpMod:
		return fmt.Sprintf("((%s) %% (%s) if (%s) != 0 else (%s))", ea, eb, eb, ea), true
	}
	return "", false
}

func (em *pyEmitter) natDispatch(app IApp, env []string) (string, bool) {
	if em.natElim == "" {
		return "", false
	}
	var args []Ir
	t := Ir(app)
	for {
		a, ok := t.(IApp)
		if !ok {
			break
		}
		args = append([]Ir{a.Arg}, args...)
		t = a.Fn
	}
	g, ok := t.(IGlobal)
	if !ok || g.Name != em.natElim || len(args) < 4 {
		return "", false
	}
	c1lam, ok := args[2].(ILam)
	if !ok {
		return "", false
	}
	inner, ok := c1lam.Body.(ILam)
	if !ok || usesVar(inner.Body, 0) {
		return "", false
	}
	out := fmt.Sprintf("_natD(%s, %s, %s)",
		em.expr(args[1], env), em.expr(args[2], env), em.expr(args[3], env))
	for _, extra := range args[4:] {
		out = fmt.Sprintf("%s(%s)", out, em.expr(extra, env))
	}
	return out, true
}

func freshPy(hint string, env []string) string {
	n := pyName(hint)
	if n == "" || n == "_" {
		n = "_x"
	}
	for {
		clash := false
		for _, e := range env {
			if e == n {
				clash = true
				break
			}
		}
		if !clash {
			return n
		}
		n += "_"
	}
}

// pyName sanitizes a rune identifier into a Python one (qualified names with dots
// mangle their dots; operators mangle through jsOpNames; primes become _).
func pyName(n string) string {
	if m, ok := jsOpNames[n]; ok {
		n = m
	} else if i := strings.IndexByte(n, '$'); i > 0 {
		if m, ok := jsOpNames[n[:i]]; ok {
			n = m + n[i:]
		}
	}
	n = strings.ReplaceAll(n, "'", "_")
	n = strings.ReplaceAll(n, "$", "_")
	n = strings.ReplaceAll(n, ".", "_")
	if n == "" {
		n = "_x"
	}
	if pyReserved[n] {
		return "_" + n
	}
	return n
}

